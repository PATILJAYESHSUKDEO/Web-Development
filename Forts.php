<h2>Other Places Found On The Fort :</h2><hr>
 
<h4>Every building or structure constructed on the fort holds a specific name for each construction. Some of the forts boast of “machi’s”, gates, walls and buildings constructed specifically. These constructions largely alter the importance of respective forts.
</h4>
<h2>Mahadarwaja:</h2><hr>
<h4>The main entrance on the Kingsway constructed to enter the fort is called Mahadarwaza. Some forts have specially designed bastions to protect the Mahadarwaja. Several others are also characterized by a number of doorways one after another in line for safety of the fort. On the inside of this Mahadarwaza, one can find a special cabin made for the guards called as the “devadi”.
</h4>
<h2>Nagarkhaana (The Drum House):</h2><hr>
<h4>The main entrance of the fort usually had a huge drum house. The opening and closing of the gates were normally accompanied by structured drum beats. On important occasions, a band of these drums often played to celebrate the event.
</h4>
<h2>Fortification:</h2><hr>
  <h4>The fort is often seen to be fortified with safety walls in the area between the “maachi” and the fortress or the citadel. These often had bastions built on them to double the safety.
  </h4>
<h2>Bastions :</h2><hr>
<h4>Bastions were constructed at a certain distance on the fortifications in various shapes. Some took the form of lotus petals while others were found to be semi-circular, triangular or hexagonal in shape. These bastions or towers had cannons mounted on them.
</h4>
<h2>Dhalkathi:</h2><hr>
<h4>Dhalkathi is a stacked structure designed on the bastion to hold the flag of the dynasty or the fort.
</h4>
<h2>Jangya (Embouchures):</h2><hr>
<h4>Specially designed cylindrical holes made in the walls or bastions to shoot guns and arrows from are called jangya(embouchures).
</h4> 
<h2>Charya(Merlons):</h2><hr>
<h4>Charya or the merlons are the specially carved or petal shaped stones kept on the bastions or the fortified walls. Soldiers could seek refuge behind these stones and open fire on the enemy when needed.
</h4>
<h2>Faanji:</h2><hr>
<h4>The patrolling lane along or on the fortified walls of the fort for the guards is known as faanji.
</h4>
<h2>Daaru Kothar (Ammunition Store):</h2><hr>
<h4>The storage room for ammunition and explosives is known is “daaru kothar”. It was often built away at safe distance from human colonies and habitations.
</h4> 
<h2>Chor Darwaza (Secret Door/Jib Door):</h2><hr>
<h4>Apart from the Mahadarwaza, every fort had other small doorways and some chor-darwaza or secret doors too. Most of the times these opened to difficult-to-climb pathways. Cleverly concealed in the fortified walls, these were often used for secret missions or undercover or confidential activities.
</h4>
<h2>Water Cisterns / Ponds / Wells:</h2><hr>
<h4>There were large tanks, ponds or wells for the purpose to have water stock on the fort. They were well taken care of as the water was an important and limited resource back then on hill tops.
</h4>
<h2>Palaces/Mansions/ Other Buildings:</h2><hr>
<h4>There were palaces, temples, buildings, etc. built for the important people living on the fort. Some forts have some distinctive buildings with special significance.
</h4> 
<h2>Shilekhana (Arsenal / Armoury):</h2><hr>
<h4>Shilekhana is the place for armours and weapons. Armourers and blacksmiths were diligently appointed to make , mend and sharpen the weapons.
</h4>
<h2>Kadelotachi Jaga (Punishment Point):</h2><hr>
<h4>Kadelot literally means pushing some off the cliff. It was the most common punishment in that era for treachery. Edge of the cliff or a big rock that protruded from the mountain was often chosen to be the punishment point or the kadelotachi jaga.
            </h4>
