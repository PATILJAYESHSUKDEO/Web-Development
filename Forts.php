          <img src="images/Forts/F26.jpg" alt="Smiley face" style="float:right;width:25%;height:40%;;">
          <h4>One of the more famous forts in Maharashtra, the Panhala Fort was the battleground in many wars, the most famous being the Battle of Pavan Khind. The fort is believed to the former home of Tarabai, the queen of Kolhapur. On the fort premises one can see the Andhar Bavadi, an underground well, the Kalavantinicha Mahal, and the Amberkhana, the site of the three granaries inside the fort.

There are also a few temples within the fort, one of them dedicated to Sambhaji II. The other is the famous Ambabai Temple, believed to be where Shivaji used to offer prayers before going on expeditions.</h4>
          </div>
          <br>
          <br>
          <br>
          <br>

          <br>
          <div>
          <p><b><h2>Vijaydurg Fort</h2></b><hr>
          <img src="images/Forts/F27.jpg" alt="Smiley face" style="float:right;width:25%;height:40%;;">
          <h4>The oldest fort in Sindhudurg, Vijaydurg is an imposing sea fort that lies on the coastline that was built during the reign of Raja Bhoja II. The fort was an important fortress of the Maratha Empire; it was used as a base to anchor their ships.

The fort earned the name Eastern Gibraltar, as it was almost impossible to conquer. The fort has seen many battles taking place here, but it has withstood all of them and till this day it lies intact. The best time to visit the fort is when the annual World Helium Day festival takes place in August.</h4>
          </div>
          <br>
          <br>
          <br>
          <br>
          <br>
          <br>
          <br>
          <br>
          <br>


          <div>
          <p><b><h2>Sindhudurg Fort</h2></b><hr>
          <img src="images/Forts/F23.jpg" alt="Smiley face" style="float:right;width:25%;height:40%;;">
          <h4>Situated on the shores of the Malvan region, the Sindhudurg Fort is spread over 43 acres of land. It took about three years to construct. The fort has a massive wall that served two purposes: it protected the fort from the sea and it prevented enemies from trespassing.

Within the limits of the fort are a few houses and temples dedicated to lords Maruti, Mahadeo, and Mahapurush. Entry to the fort during the monsoon is restricted because of the high tides that reach the top of the fort in the rainy season.Sindhudurg Fort is one of the main tourist places in Tarkarli. In the literal sense, the name Sindhudurg Fort means, fort built on the ocean floor. The fort was built by the famous Marathi king Shivaji, over a period of three years.

More than 100 Portuguese architects and thousands of Indian workers had built a fort to defend the Maratha Empire from the attacks of the outsiders or intruders. This historical site is one of the major places to visit in Tarkarli. </h4>
          </div>
          <br>

          <br>
          <div>
          <p><b><h2>Suvarnadurg Fort</h2></b><hr>
          <img src="images/Forts/F34.jpg" alt="Smiley face" style="float:right;width:25%;height:40%;;">
          <h4>Literally meaning ‘Golden Fort’, this outstanding fortification is often credited to be a “feather in the golden cap of the Marathas”. Located on a small island in the Arabian Sea between Mumbai and Goa, Suvarnadurg Fort was built during the reign of Adil Shah, but was captured in 1660 by Shivaji Maharaj. Built mainly for Navy purposes, there was once also a shipbuilding area at the fort.

Inside the fort, there is another smaller land fort called the Kanakadurga. There used to be a tunnel connecting the land forts and the sea fort which is now defunct. The only way to access Suvarnadurg now is via sea. Although there are no proper boat services available, one can ask the fishermen ferries to reach the fort.</h4>
          </div>
          <br>
          <br>
          <br>
          <br>
          <br>
          <br>
          <br>
          <br>

          <br>
          <div>
          <p><b><h2>Vasota Fort</h2></b><hr>
          <img src="images/Forts/F31.jpg" alt="Smiley face" style="float:right;width:25%;height:40%;;">
          <h4>Located in Satara district, Vasota Fort is also known as Vyaghrapada and was famously protected by Ramabai Telin, the wife of Pant Pratinidhi. Now mostly in ruins, the fort offers stunning vistas of the nearby areas, especially the Shivsagar Lake, on the banks of which this fort is situated and it is one of the best forts in Maharashtra.

Also a very popular trekking destination, this fort is seated deep within the lush valleys and forests of the Koyna Wildlife Sanctuary. Surrounded by water on three sides, the fort is famous for its traditional Maharashtrian architectural style and consists of three parts- Old Fort, New Fort and Nageshwar. Right beside the fort is the Babu Kada which is a steep U shaped cliff, offering uninterrupted views of the Old Fort. 

From the top of the fort, one can view the backwaters of the Shivsagar Dam and the entire Kas Mahabaleshwar mountain range. An ideal destination for adventure lovers, Vasota Fort offers activities like rock climbing and mountaineering.</h4>
          </div>
          <br>

          <br>
          <div>
          <p><b><h2>Rajmachi Fort</h2></b><hr>
          <img src="images/Forts/F28.jpg" alt="Smiley face" style="float:right;width:25%;height:40%;;">
          <h4>A massive fort consisting of two smaller fortresses or Balekilla, this is one of the many forts situated deep within the mesmerizing landscapes of the Sahyadri Mountains. Located near the small village of Udhewadi- which consists of just 22 homes- fortresses of the Rajmachi Fort are called the Shriwardhan Ballekilla and the Manaranjan Balekilla.

These twin fortresses are separated by a wide machi, meaning plateau, making this fort even more majestic. Declared a protected monument by the Government of Maharashtra, this fort was strategically vital in the First Anglo- Maratha War.

From this fort, one can easily spot the Bor Ghat, an important trade route between the cities of Mumbai and Pune. Hanging in midair, on top of the Shrivardhan Peak at 826 meters above sea level, the view of the Konkan region from here is breathtaking. The only way to reach this fort is by foot, either from the Udhewadi village or the Kondivade village near Karjat. </h4>
          </div>
          <br>

          <br>
          <div>
          <p><b><h2>Visapur Fort</h2></b><hr>
          <img src="images/Forts/F33.jpg" alt="Smiley face" style="float:right;width:25%;height:40%;;">
          <h4>Visapur Fort is perched atop a lush green hill of Ratnagiri district in Maharashtra. It accounts for a popular tourist excursion point amongst adventure mongers and nature lovers. With an elevation of 1084 meters from the sea level, it offers some phenomenal views of the surrounding lush green hills.

Marking its prominence from the 18th century, this fort forms a part of the fortification of Visapur and Lohagad. This gigantic architectural marvel features ancient caves, arches, wells, and old houses. A bit further, notice the beauty of two roofless buildings. As Hanuman was considered to be the patron deity of Visapur, you can find many temples dedicated to him.

Within the Visapur Fort, there’s a famous well, which is believed to be built by the Pandavas. Somewhere nearby, you could find the presence of guns and other ammunitions belonging to the reign of Queen Elizabeth. Within its vicinity, there are also remnants of Mahadev’s shrine.

Other attractions of the Visapur Fort includes few isolated caves, old house ruins, ancient water cisterns, and a huge flour mill.

Trekking to the top summit of Visapur Fort is an unforgettable adventure for every single visitor. Although the climb is a rocky one, the trek is serene and tranquil. You could either start your trekking adventure from Lohagadwadi or Patan Village. You may also try a hand at paragliding activity which takes place nearby. </h4>
          </div>
          <br>
          <br>
          <br>
          <br>


          <div>
          <p><b><h2>Prabalgad Fort</h2></b><hr>
          <img src="images/Forts/F35.jpg" alt="Smiley face" style="float:right;width:25%;height:40%;;">
          <h4>Prabalgad Fort is located in the Raigarh district of Maharashtra. It is a spectacular fort in the embrace of the Sahyadri mountain range along the western ghats. The fort is at an elevation of 700 m above sea-level on a plateau and provides vivid sight of the Irshalgad fort and the Kalyan fort.

For this reason, it also had a great historical significance as it was used as a watchtower. The fort got a rebirth under Shivaji’s reign and now it is a remarkable offbeat retreat from the exhausting city-life for the tourists. The Prabalgad terrain is covered with a green blanket of thickets.

The trek to the fort passes through dense forests through narrow trails. This terrain uphill is rocky and steep at some places with zigzag steps carved out of rocks. While ascending the fort, the plateau covered with a crown of mist makes for an awe-inspiring sight.</h4>
          </div>
          <br>
          <br>
          <br>

          <br>
          <div>
          <p><b><h2>Tung Fort</h2></b><hr>
          <img src="images/Forts/F14.jpg" alt="Smiley face" style="float:right;width:25%;height:40%;;">
          <h4>A small fort located in the outskirts of Pune, the Tung Fort was built by Adil Shah sometime before  1600 CE. It has seen plenty of wars, the famous ones being between the Marathas and the Moghuls. The Tung Fort is well known for its conical and oval shape and as a trail for experienced trekkers.

Inside the Tung Fort, there are steep steps that lead to a former water reservoir that now lies in ruins. The climb up to the Tung Fort is quite difficult to navigate through, so it’s best to go with a guide if you are planning a trek to the fort.</h4>
          </div>
          <br>
          <br>
          <br>
          <br>
          <br>
