          <div>
          <p><b><h2>Yashwantgad Fort</h2></b><hr>
          <img src="images/Forts/F22.jpg" alt="Smiley face" style="float:right;width:25%;height:40%;;">
          <h4>Located over a small hill on the Maharashtra- Goa border, this magnificent tree entangled fort sits north of the Jaitapur Creek and overlooks some of the most beautiful beaches of the south Maharashtra coast. The Yashwantgad Fort is the perfect example of how Mother Nature can take over anything that is manmade.

Roots of the trees have slowly made their way onto the walls inside the fort which have resulted in the roofless citadels. Langurs and drongo birds are present in plenty and make for a true treat to the eyes. One must pass through several gateways amidst the forest, and then cross a trench surrounding the building to be able to enter one of the most majestic forts in Maharashtra.</h4>
          </div>
