            <p><b><h1>What Is A fort: </h1><hr>
            <h4>One comes across the maximum number of forts in the western parts of Maharashtra, especially in the Sahyadris. Looking at these, one cannot help but ponder over the purpose of constructing the forts on hills or mountain reefs. Probably, the forts presented a temporary place to inhabit during wars and to keep an eye on the enemy’s movements. Also, the natural or man made inaccessibility to these forts made them just rightly protected to dwell and to attack the enemy if time be. Thus, these forts can be looked upon as havens built to safeguard the dynasty and its mark on the pages of history.</h4>
            <h2>Types Of Forts:</h2><hr>
            <h4>It’s the construction site of the fort that decides its type. Though there are various categories of forts, three broad categories are widely popular.<br> 
            These are <b>GiriDurg</b> – the hill forts, <br>
            <b>JalDurg </b>- the sea fort; usually constructed on a sea shore or those constructed on the islands in the sea and BhuDurg- the forts built on flat lands.
Apart from these, there were 
<br>
<b>Vandurg </b>– the forest forts, <br>
<b>GavhraDurg</b> – the cave forts,<br>
 <b>KardamDurg </b>- the fort is built in a mud or a marshy land, as well as only fortress or fortified buildings are considered as the forts too.
Maharashtra has been ruled by people from outer states and countries and has considerable influence of their construction skills and styles in the making of these forts. One can identify involvement and designing and building skills of non-natives right from the Africans to the Europeans. Most of these forts have been built by the rulers of the Satavahana, Chalukya, Shilahar, Yadav, Kadamb, Bahmani, Mughal, Southern Monarchies, Maratha and Peshwa dynasty. Negros, Portuguese, Dutch and British from as old as two thousand years back have had their contribution in these constructions as well.
</h4>
<h2>Important Parts Of The fort:</h2><hr>
  <h4>The commonest of all - the hill fort is often seen to be divided into four parts. The Ghera, Met, Machi and Balekilla are the main parts of this fort. The ghera is said to be the village near the foothills of the fort, Met is the non-fortified freehold plateau kind of a land between the fort and machi. Machi is the fortified plain land inside the fort and finally the citadel at the pinnacle of the fort is the most protected place on the fort.
  </h4>
  <br>
