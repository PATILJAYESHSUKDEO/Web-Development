A proper campsite has been created here due to the increase in the number of trekkers. As an alternative, one can even decide in the Menghai Temple inside the fort. During monsoons, the fort gets completely enveloped by clouds and it gets a little tricky to explore the fort properly due to poor visibility.</h4>
