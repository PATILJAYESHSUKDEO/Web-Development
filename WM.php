      <p><b><h1>Maratha Navy</h1></b><hr>
      <img src="images/Warriors/MA7.jpg" alt="Smiley face" style="float:right;width:22%;height:40%;">
      <br>
      <p><h4>The maratha Navy was the naval wing of the armed forces of the Maratha Empire, which existed from around mid-17th century to mid-18th century in India.<br><br>
       <b>Formative years</b><br> <br>
        The Maratha ruler Chhatrapati Shivaji was the founder of the navy of the Maratha military forces.

Historian Sir Jadunath Sarkar noted:

Nothing proves Shivaji’s genius as a born statesman more clearly than his creation of a navy and naval bases.

<br><br>In medieval India, the Muslim rulers (such as the Deccan Sultanates and Mughal Sultanate) had mostly ignored the naval arm of their military forces. It may be because they came overland from the North and won decisively in land battles. This scenario changed, however, when the Portuguese arrived in India and started monopolizing and controlling trade on the western coast of the continent. Chhatrapati Shivaji realized the importance of a strong navy; the first keel of a Maratha naval vessel was laid down in a creek near Kalyan circa 1654.<br><br>

Shivaji took up the task of constructing multiple naval bases along the coast of present-day Maharashtra. He organized two fleets – one under the command of Admiral Mainak Bhandari and the other under Daulet Khan. The Maratha Navy consisted mostly of native Konkani sailors; however, it was commanded mostly by mercenaries, including Siddi and Portuguese. Circa 1659, the Maratha Navy consisted of around 20 warships. Hiring mercenaries was relatively common in Maratha military culture and the Navy was no exception to this practice. The Portuguese naval officer Rui Leitão Viegas was hired as fleet commander, in part because the Maratha wanted to get insight into the Portuguese naval technology and capabilities. The Maratha knew the Portuguese had a powerful navy. The Portuguese convinced their mercenary officers to leave the service of the Maratha; however, the Portuguese allied with the Maratha when the latter went to war against the Mughal Sultanate.

<br><br>
A diorama showing Maratha naval tactics, on display at the National Museum, New Delhi
The Battle of Surat of 1664 was a well-coordinated one, whereby the Maratha used their Army and Navy in a coordinated way. In 1679, Chhatrapati Shivaji annexed the island of Khanderi, which was 11 miles (18 km) off the entrance to Mumbai. In response the English and the Siddi repeatedly attacked the island, but they were unable to oust the Maratha from the islands.

Circa 1674, during Shivaji's coronation, the Portuguese at Goa noted and acknowledged the Maratha naval power and sent their emissary to Chhatrapati Shivaji with gifts; they signed a treaty of friendship. Around this time, the Maratha Navy's strength was around 5,000 men and 57 warships. During its expedition to Karwar (present-day Karnataka), the navy possessed around 85 assorted Gallivats (warboat) ranging from 30 to 150 tons and 3 three-masted Gurabs/Grabs (warship).
        <br> <br><b>Under Sambhaji Maharaj</b><br> <br>
        <img src="images/Warfare/MN7.jpg" alt="Smiley face" style="float:right;width:25%;height:35%;">
        The Maratha Navy fought many battles during Sambhaji's reign from 1680 to 1689. Mainak Bhandari, Darya sarang and Daulat Khan were the admirals of the Maratha Navy in Sambhaji Maharaj's reign.

In the years 1678–79, Chhatrapati Shivaji Maharaj started to build the naval forts of Khanderi and Colaba near Mumbai to check the alliance between the Siddis of Janjira and the English East India Company. The construction of these forts were incomplete at the time of Shivaji Maharaj's death in April 1680. Sambhaji succeeded him and immediately completed the remaining construction on these forts. Sambhaji Maharaj fortified these positions as his strongholds.

<br><br>The Siddis of Janjira started to raid Maratha villages in Konkan under the guidance of Aurangzeb in the year 1681. Aurangzeb had planned to surround Sambhaji Maharaj's Maratha kingdom from all sides hence he ordered his subordinate Siddi to raid Maratha territories. Sambhaji was furious upon hearing this but also knew the strategic importance of Janjira Fort. He wanted to capture Janjira to dominate trade in the Arabian Sea. Sambhaji decided to punish the Siddis and capture Janjira in late 1681. Sambhaji launched an amphibious siege on Janjira with 20,000 soldiers from the army and navy, commanding the force personally. Sambhaji planned to win the fort by deceiving Siddi. He sent a party led by senior Maratha Commander Kondaji Farzand to Siddi under the pretext of a fake quarrel with him. The real plan was that Kondaji and his men would explode the gunpowder storage in the fort causing a great loss of soldiers, collapsing the walls and causing panic so that Maratha forces on shore would be able to attack and capture the shell-shocked fort. However, Siddi learned of the plan from one of the female party members and he executed the party members along with Kondaji Farzand. Only two members of the party managed to make it back to shore to Sambhaji Maharaj's camp.

<br><br>After the plan's failure, Sambhaji launched a fierce attack on Janjira. Maratha artillery started to damage the fort walls. 300 ships of the Maratha Navy were trying to attack the fort but the strong artillery of the Siddis managed to defend the fort. Both sides suffered great losses and neither side was able to gain an upper hand. Sambhaji maintained a constant pressure on Janjira and his artillery managed to inflict heavy damage to the fort walls. The Maratha Navy blockaded Janjira from three sides cutting off any supplies to the fort. Maratha forces started constructing a sea bridge from the shoreline to the fort. The bridge started to take shape. Siddis were caught in a dire situation and were battling a severe food shortage. The Siddis realised that if the same situation continues for a few days then Sambhaji would capture the fort. Hence they pleaded to Aurangzeb for help. Aurangzeb was well aware of the strategic importance of Janjira. He immediately sent General Hasan Ali Khan to destroy the Kalyan and Bhiwandi regions with a 35,000 strong force to divert Sambhaji's attack on Janjira. Hasan Ali Khan destroyed Kalyan and Bhiwandi and was threatening to attack Raigad, the Maratha capital. Sambhaji had almost captured Janjira but was forced to retreat from Janjira to check Hasan Ali Khan's advance. In absence of Sambhaji , his naval commander Dadaji Raghunath Deshpande of Mahad took control of the siege. Sambhaji later on beat back Hasan Ali Khan to Ahmadnagar but Janjira was saved due to his advance on Kalyan-Bhiwandi. Nevertheless Sambhaji and the Maratha Navy managed to inflict severe losses on the Siddis of Janjira and they never ventured against the Marathas for the rest of Sambhaji Maharaj's reign. The political goal of Sambhaji to stop the Siddis from helping Aurangzeb was achieved successfully, even though his military goal of capturing Janjira was not achieved.<br><br>

The Mughal sardars Rahullakhan and Ranmastakhan had captured Kalyan and Bhiwandi in the year 1682. They destroyed the Durgadi fort near Kalyan. Sambhaji immediately wanted to capture the cities back and arrived in the region quickly. He immediately ordered the construction of Parsik fort on the Parsik hill overlooking the Thane Creek as the Portuguese ships were using the Thane Creek to supply the enemies. Sambhaji Maharaj ordered his forces at Parsik to bombard any Portuguese supply ships crossing the Thane Creek. Sambhaji Maharaj later on defeated Rahullakhan and Ranmastakhan in the Battle of Kalyan-Dombivali and recaptured Kalyan and Bhiwandi. He immediately ordered repairs of the Durgadi fort at Kalyan. Kalyan was an important naval base for the Marathas.

<br><br>In the year 1682 the Marathas defeated the Portuguese in many small battles along the Konkan coastline, capturing Tarapur, and several other positions. Sambhaji was well aware of the strategic importance of Goa. Sambhaji Maharaj wanted to build a fort on Anjadiva Island off the coast of Karnataka in order to block Portuguese naval activity in the Goa region. His forces from the Karwar region landed on the island with construction material to construct the fort. When Portuguese got the news they reached the island and ousted the Marathas. Sambhaji Maharaj later on inflicted a crushing defeat on Portuguese in his Goa campaign of 1683. After this defeat, the Portuguese did not fight against the Marathas in rest of Sambhaji Maharaj's reign.

In late 1683 Aurangzeb had sent his son Muazzam with a force of 100,000 troops, thousands of camels, elephants and horses to save the Portuguese from Sambhaji Maharaj's attack on Goa. He ordered him to descend via the Ramdara Ghat into Goa to help the Portuguese. Muazzam's primary objective was to attack Maratha territories in Konkan from the south. Aurangzeb ordered his navy at Surat to supply Muazzam's huge force in Goa. The Maratha Navy raided these supply ships and managed to capture a large portion of these supplies. This created a huge food shortage among Muazzam's forces. Consequently Muazzam's army had to retreat because of starvation, disease and constant guerilla attacks by Maratha army.

The Maratha Navy reportedly raided Bharuch, an important trading center in Gujarat in 1687.

<br><br>Sambhaji purchased Elephanta island to check the influence of British near Mumbai.

Sambhaji wanted to modernize the Maratha Navy. Hence he allied with the Arab naval commander Jange Khan. Sambhaji invited him to Konkan to train the Maratha Navy in quick ship building and usage of artillery. Jange Khan accepted the offer and stayed with his troops in Konkan for six months in 1681. His men trained the Maratha Navy in various aspects of shipbuilding and artillery usage.

Sambhaji died in the year 1689. In his nine year reign the Maratha Navy had increased in size. He continued Shivaji Maharaj's policies to strengthen the navy. During his reign the Maratha Navy firmly held the control of coastline from Tarapur In North Konkan to Karwar in North Karnataka barring the regions of Mumbai, Janjira and Goa. He was well aware of importance of naval warfare and navy.
<br> <br><b>Under Admiral Kanhoji Angre</b><br> <br>
<img src="images/Warfare/KA.jpg" alt="Smiley face" style="float:right;width:20%;height:30%;">
After the death of Admiral Sidhoji Gujar around 1698, the Maratha Navy survived because of the extensive efforts of Koli Admiral Kanhoji Angre. Under his leadership, the British naval power was checked along the western coast of India. Kanhoji owed allegiance to supreme Maratha ruler Chhatrapati Shahu and his first minister Peshwa Balaji Vishwanath. He gained their support to develop naval facilities on the western coast of India, or Konkan. Under the leadership of Kanhoji, the Maratha developed a naval base at Vijayadurg featuring dockyard facilities for building vessels, mounting guns, and making the ships sea-worthy. Their naval fleet consisted of ten gurabs/grabs (warship) and fifty gallivats (warboat). A gallivat had a displacement lower than 120 tons, while a grab could go as high as 400 tons.

<br><br>Another ship type used was the Pal (Maratha Man-of-war), which was a cannon-armed, three-masted vessel. The grabs had broadsides of 6- and 9-pounder guns, and carried two 9- or 12-pounders on their main decks. These guns pointed forward through port-holes cut in the bulkheads. The gallivats were mostly armed with light swivel guns, but some also mounted six or eight cannons, either 2- or 4-pounders. These boats were propelled by forty to fifty oars. Even during the reign of Kanhoji Angre, the Maratha Government signed a treaty of friendship with the Portuguese in 1703. As per the treaty, the Portuguese agreed to supply cannon and gunpowder to the Maratha, supplies which they needed as they had only a few cannon foundries producing their own armaments. The Marathas signed a treaty with the Siddi as well, thus concentrating all their forces against the English East India Company.

<br><br>By the beginning of the 18th century, Kanhoji Angre controlled the entire coastline from Sawantwadi to Mumbai, which is the entire coastline of present-day Maharashtra. He built fortifications on almost all creeks, cove, and harbours, such as a fortress or citadel with navigational facilities. Any ship sailing through Maratha territorial waters was to pay a levy called Chouth, which expressed Angre's dominance.[16] Between 1717 and 1720, the East India Company made at least two attempts to defeat the Maratha Navy, but were unsuccessful. In response to a British ship being captured by Kanhoji's seamen, the British attempted to capture Vijayadurg and Khanderi, but these attempts were unsuccessful.
<br> <br><b>Battle tactics</b><br> <br>
Some of the battle tactics of the Marathas (during the reign of Admiral Kanhoji Angre) were as below:

<br><br>As far as possible, no engagement on the high seas; coastal waters were preferred, since the stronger winds at sea would benefit foreign ships because of their better spread of sail
<br><br>Attack was generally from the leeward or astern side. If enemy ships were to pursue the Maratha ships, the latter could make the use of shallow creeks and bays as a cover, where larger enemy ships could not follow
<br><br>Attack from astern ensured that the enemy ships could not bring to bear her broadside guns while Maratha Grabs could deploy its guns firing over the prow
<br><br>A constant readiness for a retreat, making use of the creeks and fort guns
<br>Enemy ships were captured by hand-to-hand combat after boarding the ship
<br><br>Spread out ships in small squadrons rather than having them all at one place
<br><br>Tire out the enemy by heavily defending the forts and avoid getting lured at open seas

By the mid 1700s, especially when compared to the Bombay Marine, the Maratha Navy declined in power rapidly. Unlike Kanhoji Angre, his successor Admiral Tulaji Angre resisted the authority of the ruling Peshwa (the de facto chief or the First Minister of Maratha Empire). The Peshwas (under Nanasaheb) (in concert with the British) engaged in a war against Tulaji, in which the British managed to get an opportunity to capture and burn a portion of the Maratha naval fleet. <br>The Peshwas reconfigured and re-establish the navy under the leadership of the Dhulap family. The British were easily able to overpower the declining Maratha Navy during the First Anglo-Maratha War. Through 1760s and 1780s, the Maratha Navy was commanded by Rudraji Dhulap and by Anandrao Dhulap. In the late 1700s, whenever the Marathas were engaged in battles or conflicts with either the British or Haider Ali of Mysore, the Maratha Navy undertook operations against enemy ships. In 1818, after the end of the third and final Anglo-Maratha War, the Angre family became a vassal of the British however a small Angre state lingered on till 1840, after which it was finally annexed to British India.

