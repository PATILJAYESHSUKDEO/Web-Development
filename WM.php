      <p><h4>The Maratha administration can be studied under three heads- Central Administration; Revenue Administration; and Military Administration. Maratha’s system of administration was largely borrowed from the administrative practices of the Deccan states. </b><br>Maratha State appointed Hindus on high post and made Marathi as an official language instead of Persian. They prepare their own state craft dictionary i.e. ‘Raja Vyakaran Kosh’ for official use. The Maratha administration can be studied under three heads- Central Administration; Revenue Administration; and Military Administration.<br>
       <br><b>Central Administration</b><br> <br>It was founded by Shivaji for the sound system of administration which was greatly inspired from the Deccan style of administration. Most of the administrative reforms were inspired from Malik Amber reforms in Ahmednagar.
       <br>The King was the supreme head of state who was assisted by a group of eight ministers known as the ‘Ashtapradhan’.
       <br><b>The Asthapradhan</b><br>

<br><b>Peshwa or the Chief Minister-</b> He looked after general administration. <br>
<br><b>Amatya or Majumdar- </b>Accountant general, he later became revenue and finance mnister. <br>
<br><b>Sachiv or Surunavis-</b> Also called Chitnis; he looked after the Royal correspondence. <br>
<br><b>Sumant or Dabir-</b> Foreign affairs and the master of Royal ceremonies. <br>
<br><b>Senapati or Sari-i-Naubat-</b> Military commander. He looked after the recruitment, training and discipline of army. <br>
<br><b>Mantri or Waqia Navis- </b>Personal safety of the king, he looked after the intelligence, post and household affairs. <br>
<br><b>Nyayadhish-</b> Administration of Justice <br>
<br><b>Punditrao- </b>Looking after charitable and religious affairs of the state. He worked for the moral upliftment of the people. <br>
<br> Apart from the departmental duties, three of the ministers- Peshwas, Schiva and the Mantri were also given incharge of extensive provinces.
All ministers, except the Panditrao and the Nyayadish, had to serve in a war whenever necessary.<br> 
Minister was assisted by a staff of eight clerks<br>

<br><b>Diwan – secretary</b> <br>
<br><b>Mujumdar – auditor and accountant</b> <br>
<br><b>Fadnis – deputy auditor</b> <br>
<br><b>Sabnis or Daftardar – office incharge</b> <br>
<br><b>Karkhanis – commissary</b> <br>
<br><b>Chitins – correspondence clerk</b> <br>
<br><b>Jamdar – treasurer</b> <br>
<br><b>Potnis – cashier</b> <br>

<br>Shivaji divided entire territory into three provinces, each under a viceroy. He further divided the provinces into Prants then Pargana and Tarafs. The lowest unit was the village which was headed by its headman or Patil.
