      <p><b><h1>Revenue Administration</h1></b><hr>
     
      <br>
      <p><h4>Shivaji abolished the Jagirdari System and replaced with Ryotwari System, and changes in the position of hereditary revenue officials which was popularly known as Deshmukhs, Deshpande, Patils and Kulkarnis.
Shivaji strictly supervised the Mirasdars who had hereditary rights in land.<br> 
The revenue system was patterned on the Kathi system of Malik Amber. According to this system, every piece of land was measured by Rod or Kathi.
<br> Chauth and Sardeshmukhi were other sources of income: Chauth was amounted to 1/4th of the standard which was paid to Marathas as a safeguard against Shivaji’s forces plundering or raiding Non-Maratha territories. Sardeshmukhi was an additional levy of 10 percent demanded from areas outside from the kingdom.</b><br><br>
<br> <br><b>Shivrai(Maratha Currency)</b><br> <br>
Shivrai was a copper coin minted during the rule of Marathas and remained in circulation till the end of the 19th century, primarily in the Bombay Presidency region.

Before 1830s, shivrai was valued at 1/74 to 1/80 of a rupee.There are 150 different types of shivrai extant till date. In 1885, the British government ordered all local revenue collectors (Mamlatdars) to collect all shivrais and deposit them in treasury. The purpose of this was to bring the new pice, worth 1/64 of rupee, in currency by eliminating this native rival. In 1890, Rev. Abbott collected and studied around 25,000 shivrais. He mentions that they were still in circulation. The shivrai remained in circulation till the end of the 19th century.
<br>When the Maratha warrior Shivaji became Chhatrapati of the Maratha empire,as a symbol of sovereignty, the Rajyaabhisheka Shaka (The Coronation Era) was started. On the occasion of coronation, special coins were minted, including a gold coin called Shivrai Hon.These coins have Shri Raja ShivaChhatrapati, in Devanagari, inscribed on them. <br>
<br> <br><b>Dudandi Shivrai</b><br> <br>
Dudandi Shivrais (Marathi: दुदांडी शिवराई) were minted during Peshwa period.Dudandi literally means two-bars.The "Shri" at the top of the obverse is underlined. This horizontal line, combined with the horizontal heading line of the word "Raja", gives us an impression of "Double bars", hence the term "Dudandi".      
The British East India Company minted Shivrais at Poona during the period of 1820-1830. Unlike Earlier Shivrais, these contained year of minting on it. The dotted collar of earlier Shivrais is also absent.[10] The year was given in Fasli era.      
