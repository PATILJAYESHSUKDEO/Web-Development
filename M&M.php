<p><b><h1>Chhatrapati Shivaji Maharaj</h1></b><hr>
<img src="images/M&M/M1.jpg" alt="Smiley face" style="float:right;width:25%;height:25%;;">
<h4>Shivaji was descended from a line of prominent nobles. India at that time was under Muslim rule: the Mughals in the north and the Muslim sultans of Bijapur and Golconda in the south. All three ruled by right of conquest, with no pretense that they had any obligations toward those who they ruled. Shivaji, whose ancestral estates were situated in the Deccan, in the realm of the Bijapur sultans, found the Muslim oppression and religious persecution of the Hindus so intolerable that, by the time he was 16, he convinced himself that he was the divinely appointed instrument of the cause of Hindu freedom—a conviction that was to sustain him throughout his life.
Collecting a band of followers, he began about 1655 to seize the weaker Bijapur outposts. In the process, he destroyed a few of his influential coreligionists, who had aligned themselves with the sultans. All the same, his daring and military skill, combined with his sternness toward the oppressors of the Hindus, won him much admiration. His depredations grew increasingly audacious, and several minor expeditions sent to chastise him proved ineffective.When the sultan of Bijapur in 1659 sent an army of 20,000 under Afẕal Khan to defeat him, Shivaji, pretending to be intimidated, enticed the force deep into difficult mountain terrain and then killed Afẕal Khan at a meeting to which he had lured him by submissive appeals. Meanwhile, handpicked troops that had been previously positioned swooped down on the unwary Bijapur army and routed it. Overnight, Shivaji had become a formidable warlord, possessing the horses, the guns, and the ammunition of the Bijapur army.Alarmed by Shivaji’s rising strength, the Mughal emperor Aurangzeb ordered his viceroy of the south to march against him. Shivaji countered by carrying out a daring midnight raid right within the viceroy’s encampment, in which the viceroy lost the fingers of one hand and his son was killed. Discomfited by this reverse, the viceroy withdrew his force. Shivaji, as though to provoke the Mughals further, attacked the rich coastal town of Surat and took immense booty.Aurangzeb could hardly ignore so flaunting a challenge and sent out his most prominent general, Mirza Raja Jai Singh, at the head of an army said to number some 100,000 men. The pressure that was exerted by this vast force, combined with the drive and tenacity of Jai Singh, soon compelled Shivaji to sue for peace and to undertake that he and his son would attend Aurangzeb’s court at Agra in order to be formally accepted as Mughal vassals. In Agra, hundreds of miles from their homeland, Shivaji and his son were placed under house arrest, where they lived under the threat of execution.

<hr><b>Shivaji’s Companions and
Associates:</b><br>
Shivaji’s Companions and
Associates: Shivaji Maharaj started the
work of founding the Swaraj in the Maval
region. The Maval terrain is full of hills
and valleys and is not easily accessible.
Shivajiraje made use of these geographical
features of Maval very skilfully for the
purpose of the foundation of the Swaraj.
He created a feeling of trust and affection
in the minds of the people. Many
companions and associates joined him in
his work of founding the Swaraj. Among
them were Yesaji Kank, Baji Pasalkar,Bapuji Mudgal, Narhekar Deshpande
brothers, Kavji Kondhalkar, Jiva Mahala,
Tanaji Malusare, Kanhoji Jedhe,
Bajiprabhu Deshpande and Dadaji
Narasprabhu Deshpande. On the strength
of these associates, Shivaji Maharaj
undertook the cause of the Swaraj.

<br>

<hr><b>Royal seal (Rajmudra) :</b><br>
The objective
of Shivaji Maharaj of establishing Swaraj
is clearly expressed in his royal seal. The
following Sanskrit lines are inscribed on
this royal seal (Rajmudra).
प्रतिपच्चंद्रलेखेव वर्धिष्णुर्विश्ववंदिता ।।
शाहसूनोः शिवस्यैषा मुद्रा भद्राय राजते ।।
Meaning : ‘This seal will grow in
splendour like the new moon. This seal
of Shivaji, the son of Shahaji, receiving
homage from the whole world denotes the
welfare of the people.’
The meaning of this inscription on the
seal is important from many angles.
Through this inscription, Maharaj has
expressed his gratitude towards his father, his confidence that Swaraj would expand
continuously, his own experience of the
respect that the Seal, that is, Swaraj
commanded from all, his commitment to
the welfare of his subjects, and the
certainty of ruling over his land
independently. This short inscription
covers the entire concept of Swaraj.
<br>

<hr><b>Towards the establishment of
Swaraj : </b><br>
The forts situated within the
jagir of Shivaji Maharaj were not under
his control, but were under the control of
Adilshah. In those days, forts were of
special significance. With a firm hold
over a fort, it was possible to control the
surrounding area. If one had forts, one
ruled the land. Therefore, Shivaji Maharaj
decided to acquire the forts that were
within his own jagir. Such an attempt to
capture the forts amounted to challenging
the Adilshahi power. He captured the
forts of Torana, Murumbdev, Kondhana
and Purandar, and laid the foundation of
Swaraj. He repaired the fort Murumbdev
and renamed it Rajgad. Rajgad was the
first capital of the Swaraj.There were the Mores of Javali, the
Ghorpades of Mudhol, the Sawants of
Sawantwadi, and other Sardars in the
Adilshahi. They were opposed to the
cause of the founding of Swaraj. It was
necessary to bring them and other
likeminded Sardars under control for the
purpose of founding the Swaraj.
<br>



<hr><b>The capture of Javali :</b><br>
Chandrarao
More of Javali in Satara district was a
powerful Sardar in the Adilshahi. He
opposed the founding of the Swaraj.
Shivaji Maharaj attacked Javali and
captured that region in 1656 ce. He
established his post there. Then he also
captured Raigad. He attained a huge
amount of wealth from Javali. After this
victory, his activities in Konkan increased.
He built the Pratapgad fort in the Javali
valley. In this way, the conquest of Javali
increased his strength in all respects. After
this, Shivaji Maharaj captured Kalyan
and Bhiwandi on the Konkan Coast.
There he came into contact with the Siddi,
Portuguese and British powers on the
Western Coast. Shivaji Maharaj realised
that in order to fight these powers, it was
necessary to have a strong naval force.
Therefore, he concentrated on building a
navy. 
<br>






<hr><b>Defeat of Afzalkhan : </b><br>
Shivaji
Maharaj had started capturing the forts in
his jagir and the surrounding area in the
Adilshahi territory. He had crushed the
opposition of the Mores of Javali. The
work of founding the Swaraj had gathered
momentum on the Konkan coast. All
these matters were a challenge to the
Adilshahi. At that time, the Badi Sahiba
was looking after the administration of
the Adilshahi. She sent Afzalkhan, a
powerful and experienced Adilshahi
General, to curb Shivaji Maharaj.Afzalkhan came from Bijapur to Wai.
He knew the Wai region well. A meeting
of Shivaji Maharaj and Afzalkhan took
place on 10 November 1659 at the foot
of Pratapgad near Wai. At the meeting,
Afzalkhan attempted treachery. In
retaliation, Shivaji Maharaj killed
Afzalkhan. He routed the Adilshahi army.
Shivaji Maharaj paid compensation
to the soldiers who were wounded in the
battle. He rewarded those who had fought
well. Those soldiers and officers of
Afzalkhan’s army who fell into the hands
of Shivaji Maharaj’s army were given a
good treatment.
<br>





<hr><b>The expedition of Siddi Jauhar : </b><br>
After the annihilation of Afzalkhan,
Shivaji Maharaj captured the Adilshahi
forts of Panhala, Vasantgad and Khelna.
He renamed Khelna as ‘Vishalgad’.
Shivaji Maharaj had posed a big
challenge before the Adilshahi. Therefore,
the Adilshah sent Siddi Jauhar, the Sardar
of the Karnul region, against Shivaji
Maharaj in 1660 ce. The Adilshah gave
Siddi the title ‘Salabatkhan’. Rustum-eJaman, Baji Ghorpade and Afzalkhan’s
son Fazalkhan were also there to help
Siddi Jauhar. In these circumstances,
Shivaji Maharaj took shelter in the
Panhala fort. Siddi’s soldiers laid siege to
the fort for about five months. Shivaji
Maharaj found himself trapped inside the
fort. Netoji Palkar tried to raise the siege
by attacking Siddi’s army from outside,
but he couldn’t succeed in it as his forces
were meagre. Siddi showed no signs of
relenting. So, Shivaji Maharaj opened
talks with him. This led to slackness in
the siege round Panhalgad.
Shivaji Maharaj took advantage of
this situation.On this occasion Shiva Kashid, a
brave youth came forward. He resembled
Shivaji Maharaj in looks. He dressed up
like Shivaji Maharaj and sat in a
palanquin. The palanquin left by the ‘Rajdindi’ gate. It was captured by the Siddi’s
army. It was a difficult situation. Shiva
Kashid sacrificed himself on this occasion
for Swaraj. In the meanwhile, Shivaji
Maharaj left the fort using another
difficult route. He was accompanied by
Baji Prabhu Deshpande, Bandal Deshmukh
and some chosen soldiers. The Siddi came
to know that Shivaji Maharaj has escaped
the siege round Panhalgad and proceeded
to Vishalgad. His army pursued Shivaji
Maharaj. Shivaji Maharaj entrusted the
responsibility of stopping Siddhi’s army
at the foot of Vishalgad to Bajiprabhu
Deshpande. Bajiprabhu Deshpande
checked Siddi’s army at the Ghod pass
near Gajapur. He fought with the greatest
valour. Bajiprabhu died a hero’s death in
this battle. Because Bajiprabhu’s army
held Siddi’s army at bay, Shivaji Maharaj
could proceed to Vishalgad. While going
to Vishalgad, Maharaj also crushed the
opposition of the Adilshahi Sardars Dalvi
of Palwan and Surve of Shringarpur.
Maharaj reached Vishalgad safely.
While Shivaji Maharaj was trapped
at Panhala, Aurangzeb, who had ascended
the throne of Delhi, had sent Shaistakhan,
a Mughal Sardar to the Deccan. He had
invaded the Pune province. The conflict
with Adilshahi continued even as the
Mughal army invaded the Swaraj. Maharaj
realised that it would not be prudent to
fight two enemies at one and the same
time. Therefore, after reaching Vishalgad
safely, he entered into a treaty with the
Adilshah. As per the treaty, he returned
the fort of Panhala to the Adilshah.
<br>

<hr><b>Escape From Agra</b><br>
Undaunted, Shivaji feigned illness and, as a form of penance, began to send out enormous baskets filled with sweets to be distributed among the poor. On August 17, 1666, he and his son had themselves carried past their guards in these baskets. His escape, possibly the most thrilling episode in a life filled with high drama, was to change the course of Indian history. His followers welcomed him back as their leader, and within two years he not only had won back all the lost territory but had expanded his domain. He collected tribute from Mughal regions and plundered their rich cities; he reorganized the army and instituted reforms for the welfare of his subjects. Taking a lesson from the Portuguese and English traders who had already gained toeholds in India, he began the building of a naval force; he was the first Indian ruler of his time to use his sea power for trade as well as for defense.

Almost as though prodded by Shivaji’s meteoric rise, Aurangzeb intensified his persecution of Hindus; he imposed a poll tax on them, connived at forcible conversions, and demolished temples, erecting mosques in their places.

<hr><b>Independent Sovereign</b><br>
In the summer of 1674, Shivaji had himself enthroned with great fanfare as an independent sovereign. The suppressed Hindu majority rallied to him as their leader. He ruled his domain for six years, through a cabinet of eight ministers. A devout Hindu who prided himself as the protector of his religion, he broke tradition by commanding that two of his relatives, who had been forcibly converted to Islam, should be taken back into the Hindu fold. Yet even though both Christians and Muslims often imposed their creeds on the populace by force, he respected the beliefs and protected the places of worship of both communities. Many Muslims were in his service. After his coronation, his most noteworthy campaign was in the south, during which he forged an alliance with the sultans and thereby blocked the grand design of the Mughals to spread their rule over the entire subcontinent.

Shivaji had several wives and two sons. His last years were shadowed by the apostasy of his elder son, who, at one stage, defected to the Mughals and was brought back only with the utmost difficulty. The strain of guarding his kingdom from its enemies in the face of bitter domestic strife and discord among his ministers hastened his end. The man that British politician and author Thomas Babington Macaulay (later Baron Macaulay of Rothley) called “the Great Shivaji” died after an illness in April 1680, in the mountain stronghold of Rajgarh, which he had made his capital.

Shivaji breathed new life into a moribund race that for centuries had resigned itself to abject serfdom and led them against Aurangzeb, a powerful Mughal ruler. Above all, in a place and age stained by religious savagery, he was one of few rulers who practiced true religious tolerance.
