<div>
<p><b><h1>Bajirao I : </h1></b><hr>
<img src="images/M&M/P1.jpg" alt="Smiley face" style="float:right;width:18%;height:50%;">
<p><h4>After the death of
Balaji Vishwanath,
Shahu Maharaj
appointed Balaji’s
son, Bajirao I the
Peshwa in 1720 ce.
He expanded the
Maratha Empire
during his term of
20 years. 
<br>
<br>
<b>Nizam’s defeat at Palkhed : </b>
<br>
The Mughal emperor Farukhsear appointed
Nizam – ul – Mulk the Subhedar of Deccan.
In 1713 ce Nizam tried to establish his
separate existence at Hyderabad. The
Emperor had given the Marathas the rights
to recover Chauthai-Sardeshmukhi from
the Mughal areas. Nizam was against it.
He captured some part of the Pune
Pargana. Bajirao decided to checkmate the
Nizam. He defeated the Nizam at Palkhed
near Aurangabad. The Nizam accepted the
Maratha right to collect ChauthaiSardeshmukhi.
As the Mughal power had become
weak, Bajirao knew that there was more
scope to expand the empire towards
northern side. Shahu Maharaj supported
his policy. 
<br>
<br>
<b>Malwa : </b>
<br>
Malwa in todays Madhya
Pradesh was part of the Mughal empire.
Bajirao sent Malharrao Holkar, Ranoji
Shinde and Udaji Pawar under the
leadership of his brother Chimajiappa to
Malwa. There, they strengthened the posts.
<br>
<br>
<b>Bundelkhand:  </b>
<br>
Bundelkhand means
some part of today’s Madhya Pradesh and
Uttar Pradesh - areas around Jhansi,
Panna and Sagar.
King Chhatrasal had established his
own kingdom in Bundelkhand. The Mughal
Subhedar Mohmmad Khan Bangush of
Allahabad attacked Bundelkhand and had
defeated Chhatrasal. Chhatrasal appealed
to Bajirao for help.
Bajirao took a large army and went
to Bundelkhand. He defeated Bangush.
Chhatrasal honoured Bajirao. This is how
Marathas established their supremacy in
Malwa and Bundelkhand.
Bajirao demanded from the Emperor
the office of the Subhedar of Malwa. As
the Emperor declined this demand, Bajirao marched on Delhi in 1737 ce. The Mughals
were taken by surprise.
<br>
<br>
<b>The battle of Bhopal : </b>
<br>
The Emperor felt uneasy on account of Bajirao’s Delhi
expedition. He invited the Nizam to
protect Delhi. Nijam marched against
Bajirao with his huge army. Bajirao
defeated him at Bhopal. Nizam agreed to
secure the sanad of Malwa subhedari for
the Marathas from the Badshah.
<br>
<br>
<b>The defeat of Portuguese : </b>
<br>
The territories of Vasai and Thane on the
Konkan coast were in the possession of
the Portuguese. The Portuguese rulers
oppressed their subjects. Bajirao sent his
brother Chimajiappa to subdue the Portuguese. Chimaji conquered Thane and
the adjacent areas. In 1739 ce, he laid
siege to the Vasai fort. The fort was very
strong. The Portuguese had a powerful
artillery. In spite of all this, Chimaji
continued the siege with perseverance and
forced the Portuguese to surrender. The
fort of Vasai and large parts of the
Portuguese territory passed into the hands
of the Marathas.
<br>
<br>
<b>Death of Bajirao : </b>
<br>
Nadirshah, the
Emperor of Iran, invaded India. Following
the order of Shahu Maharaj, Bajirao set
out for the North with a big army to
defend the Mughal power. By the time he
reached Burhanpur, Nadirshah had
returned to Iran with an enormous booty
looted from Delhi. In April 1740, Bajirao
breathed his last at Raverkhedi on the
banks of the Narmada.
Bajirao was a great General. With
his valour, he established the Maratha
supremacy in the North. He won status
for the Maratha power as a formidable
power in the whole of India.
In his time, the Shinde, Holkar,
Pawar, Gaikwad families attained
prominence.In these circumstances,
Nanasaheb tried to stabilize the Maratha
rule in the North. Ahmadshah Abdali
posed a challenge to the Marathas at
Panipat. 
<br>
<br>
<b>Situation in the North :  </b>
<br>
The region to the north-west of Ayodhya province at
the foothills of the Himalayas was called
Rohilkhand in the 18th century. The
Pathans who had migrated from
Afghanistan had settled in this region.
They were known as Rohillas. The
Rohillas had wrought havoc in the Doab
region of the Ganga and Yamuna rivers.
The Nawab of Ayodhya invited the
Marathas to subdue the Rohillas. The
Marathas chastised the Rohillas.
<br>
<br>
<img src="images/M&M/PN.jpg" alt="Smiley face" style="float:right;width:18%;height:50%;"><br>
<b>Conflict with the Afghans : </b>
<br>
Ahmadshah Abdali, the ruler of
Afghanistan, was attracted by the riches
of India. He marched on the Punjab in
1751 ce. There was anarchy in the Mughal
territory. The Mughals were facing the
threat of Abdali’s invasion. In these
circumstances, the Mughals found it
necessary for their own protection, to seek
the help of the Marathas. The Emperor
was sure of the Maratha strength and
integrity. There was no other power strong
enough to protect Delhi. Therefore, the
Emperor entered into a treaty with the
Marathas in April 1752. According to this
treaty, the Marathas agreed to protect the
Mughal power from enemies like the
Rohillas, the Jats, the Rajputs and the Afghans. In return, the Marathas were to
get cash money, the right to collect
chauthai in the territories of the Punjab,
Multan, Rajputana, Sindh and Rohilkhand
and the Subhedari of Ajmer and Agra
provinces.
As per the treaty, the Peshwa sent the
Shinde-Holkar armies for the protection of
Delhi on behalf of the Chhatrapati. When
Abdali learnt that the Marathas were
marching to Delhi, he went back to his
country. Because the threat of Abdali was
thus thwarted due to the Marathas, the
Emperor gave them Chauthai rights to the
Mughal subhas. These subhas included
Kabul, Kandahar and Peshawar. Though
the subhas were previously a past of the
Mughal empire, now they were part of
Abdali’s Afghanistan. As per the treaty, it
was a duty of the Marathas to win the
subhas back from Abdali and annexe them
to the Mughal Empire. Abdali however,
wanted to bring under Afghan control, the
region up to the Punjab. So a conflict
between the Marathas and Abdali was
imminent and inevitable.
Raghunathrao, brother of the Peshwa
Nanasaheb, accompanied by Jayappa
Shinde and Malharrao
Holkar, undertook the
campaign of North
India to combat
Abdali.
The local rulers
in the north saw the
Marathas from the
South as their
competitors. They did
not take into account
the broad vision of the Marathas and 
remained neutral, offering no help to the
Marathas. They did not like the Maratha
supremacy and interventions in the Delhi
court. Surajmal Jat and Rani Kishori were
the only ones who helped the Marathas
wounded in the battle of Panipat.
Also, some of the orthodox sects in
the North saw Marathas as people
belonging to other religions. They, too,
left aside the broad vision of the Marathas.
They persuaded Abdali to invade India
with a view to reducing the Maratha
might. They expected that Abdali would
defeat the Marathas and drive them away
back to the South, beyond the river
Narmada.
<br>
<br>
<img src="images/M&M/RP.jpg" alt="Smiley face" style="float:right;width:16%;height:20%;">
<br>
<b>The Maratha standard on Attock : </b>
<br>
Najibkhan was a Rohilla chief. He found
it hard to tolerate the Maratha supremacy
in the North. At the behest of Najibkhan,
Abdali invaded India again. This was the
fifth time he had invaded India. He
captured Delhi. He returned to Afghanistan
with a huge booty. Raghunathrao and
Malharrao Holkar marched to the North
again. They captured Delhi. Then, driving
out Abdali’s officers, they captured the
Punjab. The Marathas pursued Abdali’s
soldiers right up to Attock in 1758 ce.
The Maratha standard was raised at
Attock. Attock is in present day Pakistan.
From Attock, the Marathas pushed the
campaign up to Peshawar. However, they
did not make proper arrangements to look
after this newly conquered territory
<br>
<br>
<b>Dattaji’s valour : </b>
<br>
To get a firm hold
over the Punjab and to crush Najibkhan,
the Peshwa sent Dattaji Shinde and
Janakoji Shinde to the North. Dattaji went
to the North. Keeping Dattaji engaged in
negotiations, Najibkhan established
contacts with Abdali. He requested Abdali to come to his help. On receiving the
message from Najibkhan, Abdali invaded
India again. Dattaji and Abdali confronted
each other at Buradi Ghat on the banks
of the Yamuna. A fierce fight ensued.
Dattaji showed tremendous valour. He
met with a heroic death in this battle.
</h4></p>
