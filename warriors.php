      <p><h4>Tanaji Malusare (Marathi language: तानाजी मालुसरे), also known as Simha ("lion"), was a warrior and military leader in the army of Shivaji, founder of the Maratha Empire in 17th century India. Tanaji was one of Shivaji's closest friends hailing from the Malusare clan; the two had known each other since childhood. Tanaji is famously known for the Battle of Sinhagad in 1670. <br><br>At Shivaji's request, he pledged to recapture the fortress of Kondana near Pune. According to many accounts, he received the summons while at his son's wedding, and immediately left the festivities.He said,"aadhi lagin kondhanyacha". Tanaji and his troops scaled the fort with the help of domesticated mountain lizards (ghorpad in Marathi), to whom they tied ropes and sent crawling up to the top of the ramparts. (The ghorpad, or the Common Indian Monitor,grows up to 1.8m in length, and its natural habitat is rocky terrain, which it scales by gripping crevices with its strong claws: it is also hunted in villages as food.).
       Tanaji's pet ghorpad tried twice to scale the ramparts but failed. finally Tanaji told the ghorpad that if it failed once more, he would kill and eat it. The ghorpad successfully climbed the ramparts. Tanaji and his men than climbed the fort in pitch darkness. He and his men recaptured the fort from Udaybhan Rathod, fortkeeper of Jai Singh I. A fierce combat took place between Tanaji and Udaybhan.
      <br><br> Udaybhan managed to rid Tanaji of his shield, but Tanaji fought on by tying a cloth over one of his hands and using it to ward off Uday Bhan's sword attacks. Despite a lengthy fight, Tanaji was killed in battle, and Shivaji renamed the fort from Kondana to Sinhagad in his honor. Per legend, Shivaji's words after hearing about the demise of Tanaji were Gad ala pan Sinha gela("although the fort was captured a lion was lost").<b>Battle of Delhi, 1803</b><br><br>
      
        </h4></p>
