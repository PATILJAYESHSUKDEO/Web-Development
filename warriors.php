<p><b><h1>Maratha Army</h1></b><hr>
<img src="images/Warriors/w5.jpg" alt="Smiley face" style="float:right;width:22%;height:40%;">
<p><h4>Maratha (or Mahratta) Army refers to the land-based armed forces of the Maratha Empire, which existed from the late 17th to the early 19th centuries in India. The formation, rise, and decline of the armies of the Maratha Empire can be broadly divided into two eras
<br>
<br>
<b>17th century</b>
<br>
<br>
Chhatrapati Shivaji Maharaj, the founder of Maratha empire, raised a small yet effective land army. For better administration, Shivaji abolished the land-grants or jagir for military officers and instituted a system of salary or cash payment for their services. During the 17th century the Maratha Army was small in terms of numbers when compared to the Mughals, numbering some 100,000. Shivaji gave more emphasis to infantry as against cavalry, considering the rugged mountainous terrain he operated in. Further, Shivaji did not have access to the North Indian Mughal dominated horse markets. During this era, the armies of the Marathas were known for their agility due to the light equipment of both infantry and cavalry. Artillery was mostly confined to the Maratha fortresses, which were located on hilltops, since it gave a strategic advantage and further these fortresses had abilities to withstand sieges (such as being equipped with sufficient water supply). The Marathas used weapons like muskets, matchlocks, firangi swords, clubs, bows, spears, daggers, etc. The cavalry rode the Bhimthadi horse, which was developed by crossing Arabian and local horse breeds.


<br><br>

The Maratha Army, during Shivaji's era was systematic and disciplined. A case in point here is that the Marathas achieved success in systematic elimination of all forts which came their way during the Battle of Surat circa 1664. When it came to the artillery, Shivaji hired foreign (mainly Portuguese) mercenaries for assistance to manufacture weapons. The hiring of foreign mercenaries was not new to the Maratha military culture. Shivaji hired seasoned cannon-casting Portuguese technicians from Goa. The Marathas attached importance to hiring of experts, which can be corroborated by the fact that important posts in the army were offered to the officers in charge of the manufacture of guns.

The Army deployed musketeers as well - both regular and mercenaries. During the late 17th century, there is a mention of the Marathas using well-armed musketeers during their attack on Goa (during the reign of Sambhaji). Further, during the same period there is also a mention of Marathas using Karnataki musketeers renowned for marksmanship




</h4></p>
</div>

<div>
<p><b><h1>Structure and Rank</h1></b><hr>

<br>
<p><h4>Below was the structure and ranks of armies of the great Maratha at a high level during the reign of Chhatrapati Shivaji: Cavalry was divided into two at a high level:
<br>
<b>Shiledar:</b> A shiledar brought his own horse and equipment. Although organized different, even shiledar converged into the Sarnobat (chief of Army) <br>
<br><b>Bargir:</b> One of the lowest rank (rank and file) cavalryman of the Marathas who was provided with horse and equipment from the State's stock<br>
<br><br>The infantry consisted of the below:

<br><b>Hetkari musketeers: </b>Konkani musketeers recruited typically from the Konkan region, who possessed matchlocks and noted for their marksmanship
<b>Mavales: </b>Foot soldier recruited typically from western Maharashtra

<br>A Mahratta horseman - original pencil sketch by the artist H. Hall; standing figure in military costume with spear, left hand resting on horse
Ranks and salary of the cavalry are as below. The infantry had a similar structure
<br><br>
<b>Sarnobat (chief of Army)(a part of the Council of Eight): 4000 to 5000 hons per year</b><br>
<b>Panch Hazari: 2000 hons per year</b><br>
<b>Hazari: 1000 hons per year</b><br>
<b>Jumledar: 500 hons per year</b><br>
<b>Havaldar: 125 hons per year</b><br>
<b>Bargir: 9 hons per year</b><br><br><br>
<b>Infantry ranks (starting with senior-most rank):</b><br>

<b>Sarnobat (chief of Army)</b><br>
<b>Saat (Seven) Hazari</b><br>
