Not much is or was known about the early life of Bahirji, except that his expeditions and adventures as a spy in Chhatrapati Shivaji's maharaj army greatly contributed to the success of the Maratha Empire.[5] Bahirji was skilled at gathering detailed information about every aspect of his missions, and a lion's share of credit for many of chattrapati Shivaji's surprising feats must go to Bahirji Naik and his people.<br>
       
       
