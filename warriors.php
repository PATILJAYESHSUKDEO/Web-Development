     <br><p><b><h1>Santaji Ghorpade</h1></b><hr>
      <img src="images/Warriors/SG.jpg" alt="Smiley face" style="float:right;width:22%;height:15%;">
      <br>
      <p><h4>Santaji Mahaloji Ghorpade,(1645–1696)[citation needed] popularly known as ‘Santaji’ or ‘Santaji Ghorpade’, was one of the greatest warriors and the chief General of the Maratha Empire during Rajaram’s regime. His name became inseparable from the name of Dhanaji Jadhav with whom he made campaigns against Mughal Army continuously from 1689 to 1696. He is considered to be one of the most foremost exponents of ganimi kava (Guerilla warfare).</b><br><br>
       
      Santaji belonged to the historical Ghorpade family which is a branch of the Sisodia-Bhosale clan. Ghorpades were originally called Bhosales because of their ancestor Bhosaji sisodia of chittor. His year of birth is not known, however, it is estimated to be circa 1660. He was the eldest of three sons of[citation needed] of Mhaloji Ghorpade, who was the Senapati (General) of Sambhaji Maharaj. He had two younger brothers named Bahirji and Maloji. Mhaloji was the son of Baji Ghorpade, who was killed by Shivaji, as some sources say Baji contrived along with Afzal Khan in imprisoning and humiliating Shahji raje Bhosale (Shivaji's father) in Adilshah court. Later, however, Shivaji made peace with Mhaloji Ghorpade by providing his wise reasons for killing Baji Ghorpade. Santaji rose through the ranks under the guidance of. Hambirrao Mohite. Santaji and his younger brother Bahirji accompanied Shivaji in his campaign of Jalna in 1679. Shivaji had punished a young Santaji asking him not to show his face for three months when he ignored the retreat warning at Jalna thus causing delay to Maratha forces. Santaji later on became an important Sardar during Sambhaji Maharaj's reign. Sambhaji Maharaj had sent him and senior Sardar Keso Trimal Pingle with an army of 17,000 to bring food supplies from the Gingee region in 1686. Santaji was with his father in 1689 when Sambhaji Maharaj was being captured, while Mhaloji refused to leave Sambhaji Maharaj being his Senapati and died protecting his king in Sangameshwar. Santaji was asked to leave ahead as Sambhaji Maharaj planned to distract invading Mogul forces and escape.[citation needed]. After his untimely and unceremonious death, his sons and brothers remained loyal to Rajaram and Tarabai[citation needed] further helping the Maratha cause of Swaraj.
        <br> <br><b>Role in Maratha-Mughal War</b><br> <br>
        In the beginning of the Rajaram's regime in 1689, Santaji had attained the rank of Pancha Hajari officer i.e. commander of 5,000 soldiers. Immediately after Sambhaji's brutal torture and execution by Aurangzeb, Santaji attacked his camp at Tulapur with the help of his brothers, Bahirji Ghorpade and Maloji Ghorpade, nephew Vithoji Chavan and 2000 soldiers from Dhanaji's troops. In a daring feat, he cut the ropes of Aurangzeb's tents and took the imperial golden pinnacles and fled. Many were killed in Aurangzeb's royal tent. Initially, it was assumed Aurangzeb died too, however, he was found alive later as he was spending time in Zenat-Un-Nissa's, Auranzeb's daughter, tent. However, this incidence helped to boost the Maratha morale and restored their self-confidence to resist and attack the Mughal occupation of Maharashtra. Rajaram I conferred titles to the three Ghorpades and Vithoji Chavan for this brave attack; Santaji was given title of Mamalakatt Madaar, Bahirji was given title as Hindurao and younger brother Maloji as Ameer-ul-Umrao and Vithoji Chavan was given title as Himmat Bahadur.

        <br><br>In September 1689 along with Dhanaji, Santaji attacked Aurangzeb's General Shiekh Nizam who had placed a siege around the fort of Panhala. Nizam's army was severely beaten and his treasure, horses and elephants were captured. Then during 1689–1690 period, Santaji and Dhanaji were directed to prevent Mughal army in Maharashtra from chasing and entering Karnataka after Rajaram's flight to Jinjee. They succeeded in this task and were able to slow down and engage the Mughals in harassing skirmishes. In December 1690, Santaji and Dhanaji were promoted as leading Maratha generals, and were placed respectively under the supervision of Ramchandra Pant Amatya and Shankraji Narayan Sacheev.

        <br><br>On 25 May 1690, Sarzakhan alias Rustamkhan, a Mughal nobleman and commander, was soundly defeated and captured near Satara jointly by Ramchandra Pant Amatya, Shankraji Narayan, Santaji and Dhanaji and this proved to be a major setback to emperor Aurangzeb. In July 1692, for his great victory, Rajaram rewarded him with the Deshmukhi (fiefdom) of Miraj.

In the last quarter of 1692, Santaji and Dhanaji were sent south to alleviate the Mughal pressure on Jinjee. And on the way there they managed to capture Dharwad on 8 October 1692, Dharwad with an army consisting of 7000 Maratha foot soldiers under the duo's command.

<br><br>On 14 December 1692, Santaji defeated Aurangzeb's General Alimardan Khan, captured him and brought him back to fort Jinjee. In December 1692, the Mughal army under Zulfikhar Ali Khan around fort Jinjee was blocked and beaten by Santaji and Dhanaji as a result of which Zulfiquar khan had to sue King Rajaram for peace and was forced to compromise. Then on 5 January 1693, Santaji attacked the Mughal camp at Desur and looted their treasure, weapons and livestock.

<br><br>On 14 November 1693, Mughal General Himmat Khan beat back Santaji near Vikramhalli in Karnataka. Soon thereafter, Santaji regrouped his troops and reengaged Himmat Khan again on 21 November 1693 and avenged his earlier defeat.

<br><br>In July 1695, Santaji trapped the Mughal army camping near Khatav and harassed it with Blitzkrieg.[citation needed] Italian visitor to the Mughal court, Minnucci, has listed details of the lightning-fast and devastating Maratha attacks on the Mughal camps. High level of tension, stress and apprehension among the troops and camp followers, about the ever-present Maratha threat were recorded. On 20 November 1695, Kasim Khan, Aurangzeb's powerful General in Karnataka, was attacked, defeated and killed by Santaji at Doderi near Chitradurga.

<br><br>In December 1695, Dhanaji was defeated in a battle near Vellore by Zulfiquar Khan. On 20 January 1696 near Baswapattan, Santaji attacked, defeated and personally killed the Mughal General Himmat Khan. On 26 February 1696, Mughal General Hamid-uddin Khan defeated Santaji in a brief tussle. In April 1696, Santaji was also defeated by Zulfikhar Khan at Arani in Karnataka.

<br><br>In 1693, after lengthy negotiations with Rajaram, Zulfiquar Khan was granted a safe passage out which Santaji did not approve. Santaji had bravely defeated and captured Zuliquar Khan. It is a widely known fact that Zulfiquar Khan deliberately delayed the capture of Jingee going along with his father Asad Khan's plan to carve a territory for himself, similar to, now defunct, Adilshah and Qutubshah states in the South. They hoped and expected octogenarian Aurangzeb would die soon either due to old age or overthrown by his impatient sons for the Delhi throne. Thus, the succession chaos at the Mogul court will ensue to provide them with the opportunity to annex the southern territory, especially Golconda in Hyderabad.

<br><br>Rajaram was aware of Zulfiquar's ambitions and colluded with the Khan against Aurangzeb, probably for the sake of politics, survival and safety of future. Later in 1699, Zulfiquar also provided safe passage to Rajaram's wives unmolested when he captured Jinjee, with Rajaram already escaped. The Khan and Rajaram had understanding to benefit each other for politics or previous gratitudes. This was similar to Shahji and Radullah, later by their sons, Shivaji and Rustam, providing each other with intel of their courts. The politics then was very complex and everchanging, those acted without personal vendettas survived and were clear in their foresightedness. Zulfiquar Khan also escorted Sambhaji's family respectfully and unmolested to Aurangzeb after he captured Raigad. He also was very protective and great well wisher of Shahu's (Sambhaji's son in captivity) in Aurangzeb's campaign, probably hoped for the Maratha help for his own ambitions. Zulfiquar's mother was Shaista Khan's sister and he himself married to the Khan's daughter. Shaista Khan, who was also married to Aurangzeb's sister, lost his three fingers and pride at the hands of Shivaji, Rajaram's father, in the most famous surprise attack in Pune.

Santaji as much known to be great and intrepid in guerrilla warfare tactics did not seem shrewd in understanding the manipulation of politics and diplomacy behind curtains, and misunderstood his King and the final Maratha cause on many occasions leading to rift between him and Rajaram.

<br><br>On 8 May 1696, Santaji met Rajaram at fort Jinjee, argued with him on certain issues, some sources suggest he demanded rewards for his services, and left Jingee without resolving their differences. Santaji didn't exactly had a suave tongue like Dhanaji Jadhav and dealt much action with confrontation, bravado and brutal rage, but was highly disciplined and loyal to the Maratha cause, to overpower and subdue his opponents decisively. While meeting Rajaram, he argued and very disrespectfully said, "The Chatrapatti exist because of me and I can make and dethrone Chatrapattis at will". He probably reliased later in the fit of anger he has sealed his fate and left the place without Rajaram's permission. Dhanaji was made the new Sarnaubat(Master of Cavalry), which further enraged Santaji.

Rajaram, then did not have wise counsel of Praladji Pant, his Pratinidi (top minister), who had died. Praladji proved great talent in previously handling conflicting personalities like Santaji during the start of Maratha resurrection after Sambhaji's death. Rajaram, therefore, couldn't deal such a disrespectful provocation without reprimand in order to maintain discipline in Maratha ranks. Even though these were unpardonable provocations during Rajaram's predecessor's time or even according to Santaji's own military standards. The arrest orders were issued by the King to discipline the great warrior to avoid further mischief, but Rajaram would not have wanted him assassinated as some popular sources later suggest wrongly. Santaji was already chased by his enemies in both camps, Marathas and Moguls.

<br><br>In June 1696, by order of Rajaram, Dhanaji attacked Santaji for his rebellion near Vriddhachalam but had to turn back. In March 1697, Dhanaji defeated Santaji at Dahigaon with the help of Hanmantrao Nimbalkar.

Jadunath Sarkar, a renowned historian on Maratha history, provides a great insight in his book, House of Shivaji, about the heroics and fall of Santaji Ghorpade. Khafi Khan writes,"Shanta used to inflict severe punishments on his followers. For the slightest fault he would cause the offender to be trampled to death under an elephant." The man who insists on efficiency and discipline in a trophical country makes himself universally unpopular, and, therefore, we are not surprised when we learn from Khafi Khan that " Most of Maratha Nobles became Shanta's enemies and made a secret agreement with his rival Dhanaji Jadhav to destroy him."

<br><br>In May 1696, Dhanaji attacked Santaji but Santaji was victorious and was able to capture one of Dhanaji's key member, Amritrao Nimbalkar. Santaji later trampled him to death under an elephant. Amritrao's sister, Radhabai, was married to Nagoji Mane of Mhaswad, who then worked for Moguls. The loving sister demanded her husband to avenge the death of her brother.

In Masir-i-Alamgiri, Aurangzeb's biography, depicts an account of Santaji, "On the way to Jingee, this wretch had a fight with Dhana Jadhav, who was escorting Rajaram there, on account of an old quarrel. Shanta triumphed, and caused Amritrao, the brother-in-law of Nagoji, comrade and assistant of Dhana, to be crushed under an elephant. He also captured Rajaram but Dhana escaped. The next day Shanta appeared before Rajaram with his wrists bound together, pleading," I am the same loyal servant(as before). My rudeness was due to this that you wanted to make Dhana my equal and reach Jingi with his help. I shall now do whatever you bid me." Then he released Rajaram and conducted Rajaram to Jinjee.

Another cause of Satanji's attitude of aloofness from the government was his being drawn into the cross-currents of ministerial rivalry of the western capital of Maharashtra. He sided with Parshuram the rival of Ramchandra Pant, otherwise known as Amatya. Dhana Jadhav was preferred by Rajaram and Ramchandra Pant, latter was more of regeant to Rajaram and conducted all his affairs in Maratha resurrection after the death of Sambhaji. Dhana was also well praised in Mogul records and preferred in any negotiations arose between Marathas and Mogul Chieftains. Dhana was the great-grandson of Jijabai's brother. Jijabai was also the grandmother of Rajaram. Santaji was possibly the grandson of Baji Ghorpade, who arrested and humiliated the grandfather of Rajaram, Shahji in Adilshah court in 1648. This, however, unlikely had any bearing on the strained relations between two factions. Shahji Raje helped many of his Maratha relatives rise to power, and Ghorpades also benefitted greatly from his benevolence.

Either Santaji felt under-appreciated and expected more rewards, or others felt his means and methods brutal and distasteful, he certainly could have been managed well if Rajaram had a good counsel of ministers to handle such personalities through diplomacy. Then, there were known devious means by Moguls as well to create rift within Marathas using individual factions' insecurities. In all likelihood Santaji, much like his previous King Sambhaji, took pride in his bravery and was loyal to Maratha cause of Swaraj to be seduced by any of enemy bribes or tactics.

<br><br>In July 1697, Maratha Sardar Nagoji Mane killed Santaji in the forest of Karkhel while Santaji was performing religious rites on banks of a local river to avenge death of his sisters husband . The head was paraded with big pomp and trumpets by Mogul soldiers, most likely to reduce fear than celebrate the demise of most dreaded enemy. Thus, weakening Maratha cause for some time after Santaji's death.

The Marathas from time to time lost such great heroes and lost the momentum of much-revered Swaraj. However, his compatriot, Dhanaji Jadhav didn't forget him. Till his death in 1708, he continued campaigns in his name. Together, they had done a lot, now without Santaji, things were harder, but hope wasn't lost.
