<div>
<p><b><h1>During the War of 27 Years</h1></b><hr>
<img src="images/Wars/w7.jpg" alt="Smiley face" style="float:right;width:25%;height:60%;">
<br>
<p><h4>During the War of 27 Years (1680 to 1707) the Maratha State forces (regular army) dispersed and the theater of warfare became entire Southern India (or Deccan India). During this period the armies of the Marathas resorted to guerrilla warfare; bands of irregular soldiers also joined in and it became a People's War. Raiding enemy's rear, isolated posts, supply lines was a feature during this period. Common men and women from virtually every town and village offered shelter to the Mahratta forces led by two valiant Generals - Santaji Ghorpade and Dhanaji Jadhav

Jadunath Sarkar the noted historian writes in his famous book namely military history of India about Santaji Ghorpade, a brilliant strategist who fought the Mughals in the 27 year war :

"He was a perfect master of this art, which can be more correctly described as Parthian warfare than as guerrilla tactics, because he could not only make night marches and surprises, but also cover long distances quickly and combine the movements of large bodied over wide areas with an accuracy and punctuality which were incredible in any Asiatic army other than those of Chengiz Khan and Tamurlane".
</h4></p>
</div>




<div>
<p><b><h1>18th century</h1></b><hr>
<img src="images/Wars/w8.jpg" alt="Smiley face" style="float:right;width:25%;height:60%;">
<br>
<p><h4><b>Pre-1761</b><br>
<br>
During the 18th century the Maratha army continued its emphasis on its light cavalry, which proved better against the heavy cavalry of the Mughals. Post 1720, the armies of the Maratha Empire, in the reign of Chhatrapati Shahu started making their presence felt in Northern India (the bastion of the Mughals) and scored numerous military victories, primarily due to the skills of his Peshwa Bajirao I as a great cavalry leader and military strategist. Bajirao Peshwa made excellent use of small and heavy ammunition (using it in excellent coordination) and used smothering tactics. The Marathas led by Bajirao I would use their artillery to create a blanket of projectiles to smother the enemy.


A Maratha cannon (Artillery battery), as seen at Raigad Hillfort
A hallmark of Peshwa Baji Rao I's contingents was that of long-distance cavalry attacks, typically light and agile cavalry. During the reign of Chhatrapati Shahu, the cavalry strength was some 100,000. Chhatrapati's own cavalry was called as the Huzurat Cavalry which was an elite cavalry division led by the Peshwa. Further, Baji Rao used massed ranks of infantry consisting of flintlock-armed regulars under South Asian officers.
<br><br><b>Modernization pre-1761</b>
<br>
<br>
When the Marathas confronted the French (allies of the Nizam) on battlefield in 1750s, they realized the importance of western-style disciplined infantry. Hence the process of modernization began even before the Third Battle of Panipat (1761). Sadashivrao Bhau admired Western-style disciplined infantry.Circa 1750s, the Marathas endeavored to hire the services of the French General Marquis de Bussy-Castelnau (who served in the Nizam’s Army) for training purposes, but when they failed in their efforts, they managed to hire Ibrahim Khan Gardi. Ibrahim Khan was an artillery expert trained under the leadership of Bussy. The word gardi is a corruption of the French word garde (guard) and this gardi formed the backbone of Maratha infantry. Ibrahim Khan played a major role in re-configuring the Maratha artillery. He served the Marathas in the infamous Third Battle of Panipat. During the battle, out of the approximate 40,000 Maratha Army men, some 8000 or 9000 were artillery (Gardi Infantry). They possessed 200 cannons (consisting of heavy field-pieces as well as light camel or elephant-mounted zambaruks (a swivel gun equivalent) and also possessed handguns.

During this era, sources state that the Marathas made use of both flintlocks and matchlocks and that their matchlocks had a technological advantage having superior range and velocity.However at Third Battle of Panipat, they possessed mainly just swords and spears whilst Abdali possessed a larger force with flintlock muskets.
<br><br><b>Modernization post-1761</b><br><br>From the 17th century till the mid-18th century the artillery of the Marathas was more dependent on foreign gunners rather than their own.


A Mahratta field artillery gun on display at Shaniwar Wada, Peshwa's Palace, in Pune
Post 1761 Mahadaji Shinde, a distinguished Maratha Maharaja, focused his attention on European artillery and secured the services of the noted Frenchman Benoît de Boigne who had received training from the best of the European military schools. Following suit, the other Maratha chiefs such as the Peshwas, the Holkars, the Bhosales, also raised French-trained artillery battalions.

<br><br><b>Chhatrapati's Cavalry: The elite Huzurat cavalry were equipped with matchlocks instead of long spears.</b><br>
<br><b>Scindia's infantry organization and weapons under De Boigne: De Boigne organized his infantry under campoos. One campoo had 10 infantry battalions, which consisted of 4000 infantrymen equipped with flintlocks and bayonets. Each battalion had 5 guns (1 howitzer and 4 field guns) and every battalion was commanded by a European gunner. Each campoo had 30 siege guns (16 and 24 pounder guns), apart from mortar guns, howitzers, and camel-mounted blunderbusses. Five campoos were raised between 1790 and 1803. The artillery had 50 bronze cannons (half of which were big caliber guns).</b><br>
<br><b>Training: Further there was emphasis on training and infantrymen were trained on handling firearms, gunnery practice, and military manoeuvres.</b><br>
<br><b>Innovations: Under De Boigne a new weapon was invented having six musket barrels joined together.</b> <br>
<br><b>Ambulance Corps: Another novelty was the ambulance corps in charge of providing aid to wounded soldiers (including enemy soldiers) </b><br>
<br><b>Composition: Under Mahadji Scindia, Rajput and Muslim infantrymen were recruited in the Maratha Army. Further, his cavalrymen had a different uniform - long trousers as opposed to the shorter Deccani ones</b><br>
<br><b>Military-Industrial Complex: Further circa 1784, Mahadaji Shinde established a military-industrial complex for the armies of the Maratha near Agra. The ordnance factories of the Marathas made use of sophisticated indigenous technologies with more of adaptation as against innovation. Mahadaji Shinde created one of finest armies in India, with the help of the French and Portuguese and it also included a brigade known as Deccan Invincibles, which numbered some 27,000.</b><br>
<br>There is a mention of induction of 6 pound field guns under the command of Pierre Perron, Maratha General of Scindia dominions (the successor of De Boigne). These 6 pound guns were inducted after phasing out the earlier light 3 pound ones.
In a bid to Westernize the artillery, circa 1777, there is a mention of a Portuguese officer named Naronha heading the Peshwa’s artillery and further he had a number of European artillery men working under him. Circa 1790, the Peshwa's forces employed a Portuguese officer for casting 40-pounder guns.
The Nagpur Senasahebsubah Bhonsle's forces had two Westernized infantry brigades, however without any European officers, while Scindia's forces were the most Westernized.
<br><br>
The arms of the Marathas
However a point to be noted here is that it is untrue to conclude that armies of the Maratha got disciplined only post 1761. The Marathas were well aware of the importance of discipline and disciplined and drilled infantry in Maharashtra existed even in the Hindu classical era. The Mahrattas were aware of the Portuguese infantry models having concepts like "Spanish square" since sixteenth century.

In the late 18th century and early 19th century, with French-trained artillery and infantry, the Marathas managed to regain their lost ground in North India, however they could not match the superior artillery of the British East India Company, which in due course of time, among other reasons, led to the defeat of the Marathas at the Third Anglo-Maratha War and decline of their Empire itself.
<br><br><b>Employment of the Pindaris</b><br><br>
Pindaris were irregular horsemen and their primary role was to plunder in return of payment. Pindaris composed of both Muslims and Hindus. They had implicit support from Maratha chiefs (Maharajas) such as Scindias of Gwalior, Holkars of Indore, and Bhosales of Nagpur. This band of freebooters accompanied Maratha forces during their campaigns and helped win wars in return for plunder and pay. They were a part of the Maratha Army during the Third Battle of Panipat and almost all Anglo-Maratha Wars.
  <br><br><b>Employment of Bargis</b><br><br>
The Nagpur Bhosales employed thousands of units called Bargi to invade Mughal Bengal. The invasions lasted annually for ten years until finally the Nawab of Bengal, European merchants and locals had built the Maratha Ditch to safeguard themselves from war.
The Bargi were involved in several war crimes.
<br><br><b>Possible employment of Gurkha</b><br><br>
The Peshwa was very successful in deploying forces in Northern India. There are indications that India's native Gurkha may have sided with the Maratha during their attacks upon Delhi, the seat of Mughal rule.
<b></b><br><br>

</h4></p>
