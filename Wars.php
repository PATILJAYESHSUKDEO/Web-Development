        <br><br><b>Battle of Malpura</b><br><br>
        The Battle of Malpura was a battle in 1800 between the Kingdom of Jaipur (ruled by the Kacchwahas) and supported by the Kingdom of Marwar (ruled by the Rathores) against the Kingdom of Gwalior (ruled by the Scindias). It was the result of a crisis between the governments of the two sides.

In 1800, a crisis developed between Jaipur and Gwalior Scindia`s Government. The main reasons behind it were:

Burden of more money on Rajput kings annoyed them.
Rajputs sensed opportunity in the civil war between Scindhia and Holkar in Poona.
There was same internal conflict in Daulat Rao Sindhia's administration. The feud between the widow of Mahadaji and disruption by his old servant and such condition ensured that Scindia remained in the limit of north India.
Sawai Pratap Singh made an attempt to profit from these internal dissensions of his Scindia rivals prior to the Battle of Malpura. In March 1800, he openly rejected the money clauses of the treaty of 1791 (signed after the Battle of Patan and started to prepare for war. Pratap Singh asked all the Rajput Rajas to join him, but the Maharaja of Marwar was the only one who answered, the Rathors were eager to win back the territories they had lost to the Sindhia's. 5000 Rathor horsemen were sent from the capital of Jodhpur under the able command of Sawai Singh Rathor to assist Sawai Pratap Singh. The Jaipur raja gathered an army of his clansmen along with Gosains, Rohillas and irregulars while the Gwalior army had the Campoos infantry, made-up of Rajputs and muslims,[3] and a contingent of Maratha cavalry to support them. The two armies met at Malpura, thus paving the way for the battle.
        
