        <p><h4>The Maratha Conquests were a series of conquests in the Indian subcontinent which led to the building of the Maratha Empire. These conquests were started by Shivaji in 1659 from the victory at the Battle of Pratapgad against Bijapur. The expansion of the empire was limited and interrupted by the Mughal conquests of south India by Emperor Aurangzeb. Marathas were forced to defend their territories against the overwhelmingly strong Mughal army in the 27 years long Deccan wars. They were able to defend their territories and gain an upper hand over Mughals in the sustained conflict.  <br>  <br>

Afterwards, the Marathas conclusively defeated and overtook major territories of the Mughal Empire in the Indian subcontinent and its vassals. It ended with the eventual fall of the Maratha Empire after the Anglo-Maratha Wars.After defeating the Mughals and the Bijapur Sultanate in multiple battles from 1645 to 1674, Shivaji founded an independent Maratha kingdom in 1674 with Raigad as its capital. Shivaji died in 1680. After Shivaji, Sambhaji took up throne. He built strong army as well as navy. Mughal emperor Aurangzeb shifted his capital from Delhi to Aurangabad to defeat Sambhaji. The Mughals invaded, fighting an War of 27 years from 1681 to 1707 in which the Marathas under Tarabai were victorious. Sambhaji was captured during this war and killed by Mughals. Shahu, a grandson of Shivaji, ruled as emperor until 1749. During his reign, Shahu appointed the first Peshwa as head of the government, under certain conditions.  <br>  <br> After the death of Shahu, the Peshwas became the de facto leaders of the Empire from 1749 to 1761, while Shivaji's successors continued as nominal rulers from their base in Satara. Covering a major part of the subcontinent, the Maratha Empire kept the British forces at bay during the 18th century, until internal relations between the Peshwas and their sardars (army commanders) deteriorated, provoking its gradual downfall.

<br>  <br>  <br>The Maratha Empire was at its height in the 18th century under Shahu and the Peshwa Baji Rao I. Losses at the Third Battle of Panipat in 1761 suspended further expansion of the empire in the North-west and reduced the power of the Peshwas. In 1761, after severe losses in the Panipat war, the Peshwas slowly started losing the control of the kingdom. Many military chiefs of the Maratha Empire like Shinde, Holkar, Gaikwad, PantPratinidhi, Bhosale of Nagpur, Dev (Gade) of Wardha, Pandit of Bhor, Patwardhan, and Newalkar started to work towards their ambition of becoming kings in their respective regions. However, under Madhavrao Peshwa, Maratha authority in North India was restored, 10 years after the battle of Panipat. After the death of Madhavrao, the empire gave way to a loose Confederacy, with political power resting in a 'pentarchy' of five mostly Maratha dynasties: the Peshwas of Pune; the Sindhias (originally "Shindes") of Malwa and Gwalior; the Holkars of Indore; the Bhonsles of Nagpur; and the Gaekwads of Baroda. A rivalry between the Sindhia and Holkar dominated the confederation's affairs into the early 19th century, as did the clashes with the British and the British East India Company in the three Anglo-Maratha Wars. In the Third Anglo-Maratha War, the last Peshwa, Baji Rao II, was defeated by the British in 1818 and the empire ceased to exist.
