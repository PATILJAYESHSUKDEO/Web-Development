      <img src="images/Wars/AM3.jpg" alt="Smiley face" style="float:right;width:30%;height:60%;">
      <br>
      <p><h4>The Third Anglo-Maratha War (1817–1818) was the final and decisive conflict between the British East India Company (EIC) and the Maratha Empire in India. The war left the Company in control of most of India. It began with an invasion of Maratha territory by British East India Company troops,and although the British were outnumbered, the Maratha army was decimated. The troops were led by Governor General Hastings, supported by a force under General Thomas Hislop. Operations began against the Pindaris, a band of Muslim mercenaries and Marathas from central India.

      <br>        <br>Peshwa Baji Rao II's forces, supported by those of Mudhoji II Bhonsle of Nagpur and Malharrao Holkar III of Indore, rose against the East India Company. Pressure and diplomacy convinced the fourth major Maratha leader, Daulatrao Shinde of Gwalior, to remain neutral even though he lost control of Rajasthan.

British victories were swift, resulting in the breakup of the Maratha Empire and the loss of Maratha independence. The Peshwa was defeated in the battles of Khadki and Koregaon. Several minor battles were fought by the Peshwa's forces to prevent his capture.

<br>  <br> The Peshwa was eventually captured and placed on a small estate at Bithur, near Kanpur. Most of his territory was annexed and became part of the Bombay Presidency. The Maharaja of Satara was restored as the ruler of his territory as a princely state. In 1848 this territory was also annexed by the Bombay Presidency under the doctrine of lapse policy of Lord Dalhousie. Bhonsle was defeated in the battle of Sitabuldi and Holkar in the battle of Mahidpur. The northern portion of Bhonsle's dominions in and around Nagpur, together with the Peshwa's territories in Bundelkhand, were annexed by British India as the Saugor and Nerbudda Territories. 
The defeat of the Bhonsle and Holkar also resulted in the acquisition of the Maratha kingdoms of Nagpur and Indore by the British. 
<br>Along with Gwalior from Shinde and Jhansi from the Peshwa, all of these territories became princely states acknowledging British control.<br> 

<br>The British proficiency in Indian war-making was demonstrated through their rapid victories in Khadki, Sitabuldi, Mahidpur, Koregaon, and Satara.
