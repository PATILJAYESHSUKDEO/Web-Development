        <p><h4>
        <b>Battle of Pavankhind</b>
        <br>    <br>

The Battle of Pävankhind was a rearguard last stand that took place on 13 July 1660 at a mountain pass in the vicinity of fort Vishalgad, near the city of Kolhapur, Maharashtra, India between the Maratha Warrior Baji Prabhu Deshpande and Siddi Masud of Adilshah Sultanate. The engagement ending with the destruction of the Maratha forces, and a tactical victory for the Bijapur Sultanate, but failing to achieve a strategic victory. Baji Prabhu Deshpande managed to engage a large Adilshahi army with 300 soldiers, while Shivaji managed to escape the siege.
<br>    <br>
<b>Battle of Umberkhind</b>
<br>    <br>
The Battle of Umberkhind took place on 3 February 1661 in the mountain range of Sahyadri near the city of Pen, Maharashtra, India. The battle was fought between the Maratha under Shivaji and Kartalab Khan of the Mughal Empire. The Marathas decisively defeated the Mughal forces. This battle was a great example of guerrilla warfare.
<br>    <br>
<b>Battle of Surat</b>
<br>    <br>
The Battle of Surat took place on 5 January 1664, near the port city of Surat between Maratha ruler Chhatrapati Shivaji and Inayat Khan, a Mughal captain. The Marathas defeated the Mughal force, and sacked the city of Surat for six days. The loot was then transferred to Rajgad fort.
<br>    <br>
<b>Battle of Sinhagad</b>
<br>    <br>
The Battle of Sinhagad took place during the night on 4 February 1670 on the fort of Sinhagad (then known as Kondhana) between subedar Tanaji Malusare, commander of the Maratha Empire and Udaybhan Singh Rathore, the fortkeeper under Jai Singh I who worked for the Mughal emperor Aurangzeb. Climbing up the fort, the Marathas were intercepted by the Mughal garrison and combat ensued between the guards and the few infiltrators that had managed to climb up by this time. Both Tanhaji and Udaybhan were killed in the battle but the overwhelming Maratha forces managed to capture the fort after the reinforcements penetrated the gateway of the fort from another route.
<br>    <br>
<b>Battle of Salher</b>
<br>    <br>
The Battle of Salher which was a battle fought between the Maratha Empire and the Mughal Empire in February 1672 CE. The battle was fought near the fort of Salher in the Nashik district. The result was a decisive victory for the Maratha Empire. This battle is considered particularly significant as it is the first battle in which the Mughal Empire lost on an open field. Mughal empire started to decline after this battle and the battle of Dindori fought one year earlier.
<br>    <br>
<b>Battle of Vikramgad</b>
<br>    <br>
Battle of Vikramgad was a land battle that took place in the year 1672 near Vikramgad, Palghar district, Maharashtra between the Maratha Empire and the Mughal Empire. The Marathas were led by prince Sambhaji and Moropant and the Mughals were led by Khijr Khan. The Marathas won a decisive victory and it consolidated their territorial gains in Kolwan.
<br>    <br>
<b>Battle of Bhupalgarh</b>
<br>    <br>
The Battle of Bhupalgarh occurred between the Mughal and Maratha empires in 1679. The battle resulted in the defeat of the Marathas and capture, loot and razing of the fort of Bhupalgarh under Firangoji Narsala by the Mughal forces led by Diler Khan.
<br>    <br>
<b>Battle of Sangamner</b>
<br>    <br>
The Battle of Sangamner was fought between the Mughal Empire and Maratha Empire in 1679. This was the last battle in which the Maratha king Shivaji fought. The Mughals had ambushed Shivaji when he was returning from a sack on Jalna. The Marathas engaged in battle with the Mughals for three days until Maratha General, Sidhoji Nimbalkar was killed alongside 2,000 Maratha soldiers. The Maratha force was largely decimated defending their king, however Shivaji managed to retreat with 500 men.
