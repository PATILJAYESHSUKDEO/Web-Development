        <p><h4>
        <b>Restoration of Maratha suzerainty in the North</b><br><br>
        Under Madhavrao Peshwa, Maratha authority in North India (including Delhi) was restored ten years after the battle of Panipat. The Rohillas were defeated and were forced to pay a heavy war indemnity. Delhi was captured by Mahadji Scindia in late 1770 and restored Mughal emperor Shah Alam II to the throne of Delhi in 1772.

        <br><br><b>Battle of Alegaon</b><br><br>
        The Battle of Alegaon was fought between Nizam Ali Khan of Hyderabad and Raghunathrao of the Maratha Empire against Peshwa Madhavrao of the Maratha Empire.[16] Raghunathrao had established an alliance with Nizam Ali Khan of Hyderabad.[16] When conflict arose between Raghunathrao and Madhavrao I, a joint campaign between Nizam Ali Khan and Raghunathrao resulted in Madhavrao I being heavily defeated.[16] Peshwa Madhavrao surrendered on November 12, 1762.[17] Nizam Ali Khan got all of his previously lost territories that were lost at the Battle of Udgir. Peshwa Madhavrao submitted to his uncle, Raghunathrao.

        <br><b>Battle of Rakshasbhuvan</b><br>
