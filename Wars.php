        War of 27 years was a series of battles fought between Marathas and Mughals from 1681 to 1707 in the Indian subcontinent. It was a series of battles. The war started in 1680 with the Mughal emperor Aurangzeb’s invasion of the Maratha enclave in Bijapur established by Shivaji.
          <br><b>The war can be broken down into three distinct phases :<br>

          <br>Marathas under Chatrapati Sambhaji Maharaj(1681–1689).<br>
          <br>Marathas under Chatrapati Rajaram Maharaj (1689–1700).<br>
          <br>Marathas under Maharani Tarabai (1700–1707).</b><br>
          <br>
          It was a long snakes and ladders war game involving a quarter of a century and innumerable long and short battles. The war ended with the ultimate defeat and death of Aurangzeb in 1707. It also paved the way for the Maratha expansion in the North.
