        <p><h4>
        <br><b>First Battle of Katwa</b><br>
        <br>
        The Battle of Katwa occurred between the Nawab of Bengal and Maratha Empire in 1742. The Maratha's initially attacked and captured Katwa and Hooghly, in Bengal. The Nawab of Bengal Ali Vardi Khan responded with a direct attack at the Maratha camp at Katwa in the nightfall, so much that the entire Maratha army evacuated out of Bengal on September 17, 1742 believing a much larger force had charged them.

        <br><br><b>Second Battle of Katwa</b><br>
        The Second Battle of Katwa occurred between the Nawab of Bengal and Maratha Empire in 1745. After the initial evacuation of the Maratha's at the First Battle of Katwa, the Maratha General, Raghuji Bhonsle attempted, once again, to conquer Katwa. Bhonsle, with 20,000 horsemen attacked Murshidabad then moved onwards to Katwa, where Nawab of Bengal Ali Vardi Khan fought Raghuji and his men, with Marathas moving in towards Medinipur in Bengal.



        <br><br><b>Battle of Burdwan</b><br>
        <br>The Battle of Burdwan occurred between the Nawab of Bengal and Maratha empires in 1747. After the dismissal of Mir Jafar by Ali Vardi Khan, an army was amassed to defend against the invading Maratha forces of Janoji Bhonsle at Orissa. Ali Vardi Khan managed to heavily repulse and defeat the Maratha's in this battle.



        <br><br><b>Battle of Delhi, 1757</b><br>
        <br>After a victorious Battle of Narela on 16 January 1757 Maratha Army led by Antaji Mankeshwar and an advance column of Ahmad Shah Abdali's army, the Battle of Delhi was a battle fought on 11 August 1757 between Maratha Empire under the command of Sadashivrao Bhau and Rohilla Afghans under Najib-ud-Daula. The battle was waged by the Marathas for the control of Delhi, the former Mughal capital which was now under the control of Rohilla chief Najib-ud-Daula, as a consequence of fourth invasion of Ahmad Shah Abdali.

     
