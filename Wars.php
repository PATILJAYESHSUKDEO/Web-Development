        <br>  <br>
        Siege of Ramsej (1682-1688) was a series of military confrontations between the Maratha Empire headed by Sambhaji and the Mughal Empire led by Aurangzeb regarding the control of Ramsej Fort in the Nashik region. Aurangzeb arrived in the Deccan in late 1681 with a 500,000 strong army to destroy the Maratha Empire and the Deccan Sultanates of Adilshahi and Qutubshahi. He wanted to capture the forts held by the Marathas in Nashik and Baglana region. Hence he decided to begin his Deccan campaign with an attack on Ramsej Fort which is near Nashik.Siege of Ramsej (1682-1688) was a sustained military conflict between Maratha Empire led by Sambhaji Maharaj and Mughal Empire led by Aurangzeb for the control of Ramsej fort in Nashik district of Maharashtra. The 600 Marathas under Killedar Suryaji Jedhe were able defend the fort against the overwhelmingly strong Mughal army for six years. After 5 years Suryaji Jedhe was transferred to another fort according to the rotation policy of the Maratha Administration. Mughals bribed the new Killedar and captured the fort in 1688. The heroic defence of Ramsej by a handful of Marathas became a symbol of Maratha resistance against Aurangzeb.And took the oath that he would not wore his badshahi turban until and unless he kill's Chhatrapati Sambhaji Maharaj
