        <b>Battle of Palkhed</b><br>  <br>
        Battle of Palkhed was a land battle that took place on 28 February 1728 at the village of Palkhed, near the city of Nashik, Maharashtra, India between the Maratha Peshwa, Baji Rao I and the Nizam-ul-Mulk of Hyderabad. The Marathas defeated the Nizam. The battle is considered an example of brilliant execution of military strategy.
        <br>  <br><b>Battle of Mandsaur</b><br>  <br>
        The Battle of Mandsaur took place in Mandsaur, modern day Madhya Pradesh between Marathas, commanded by Malharrao Holkar, and Jai Singh of Amber, in which Jai Singh was defeated in February, 1733. Malhar Rao Holkar then conquered Bundelkhand and Bundi.

        <br>   <br><b>Battle of Delhi</b><br>  <br>
        In November of 1736, the Maratha Peshwa Baji Rao I advanced on Old Delhi to attack the Mughal capital. Mughal emperor Muhammad Shah sent Saadat Ali Khan I with a 150,000-strong army to stop the Maratha advance on Delhi. But Baji Rao's subordinate chiefs Malhar Rao Holkar and Pilaji Jadhav crossed the river Yamuna and looted Ganga-Yamuna Doab. Saadat Khan then retired to Mathura, thinking the Marathas had retreated towards Pune. But Baji Rao's army advanced to Delhi and encamped near Talkatora. Muhammad Shah sent Mir Hasan Khan Koka with an army to intercept Baji Rao. The Mughals were devastated by the fierce Maratha attack, and lost half of their army, which compelled them to ask for all regional rulers to help against the army of the Marathas. After the battle, when the news of Saadat Ali Khan's approaching large Mughal army reached Baji Rao, he retreated to Pune.

        <br>
        <br><b>Battle of Bhopal</b><br>  <br>
        The Battle was fought between the Maratha Empire and Mughal forces led by Nizam of Hyderabad near Bhopal in India in December 1737. The Marathas poisoned the water and the replenishment supplies of the besieged Mughal forces. Chimaji was sent with an army of 10,000 men to stop any reinforcements while Bajirao blockaded the city instead of directly attacking the Nizam. The Nizam was forced to sue for peace after he was denied reinforcements from Delhi.[12] The battle resulted in decisive Maratha victory mainly through the swift tactics of Maratha Peshwa Baji Rao.

        <br> <br><b>Battle of Vasai</b><br>  <br>
        The Battle of Vasai was fought between the Marathas and the Portuguese rulers of Vasai, a village lying near Bombay in the present-day state of Maharashtra, India. The Marathas were led by Chimaji Appa, brother of Peshwa Baji Rao I. Maratha victory in this war was a major achievement of Baji Rao I reign.
